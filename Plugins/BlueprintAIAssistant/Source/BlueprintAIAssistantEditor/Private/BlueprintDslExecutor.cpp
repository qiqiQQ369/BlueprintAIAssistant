static bool ParseOptionalBoolToken(const FString& Token, bool bDefault)
{
	const FString T = Token.TrimStartAndEnd();
	if (T.IsEmpty())
	{
		return bDefault;
	}
	if (T.Equals(TEXT("true"), ESearchCase::IgnoreCase) ||
		T.Equals(TEXT("loop"), ESearchCase::IgnoreCase) ||
		T.Equals(TEXT("1"), ESearchCase::IgnoreCase))
	{
		return true;
	}
	if (T.Equals(TEXT("false"), ESearchCase::IgnoreCase) ||
		T.Equals(TEXT("0"), ESearchCase::IgnoreCase))
	{
		return false;
	}
	return bDefault;
}

