static bool TryConnectHitResultToCastObject(
	UEdGraph* Graph,
	UEdGraphPin* FromPin,
	UEdGraphNode* ToNode,
	UEdGraphPin* ToPin)
{
	if (!Graph || !FromPin || !ToNode || !ToPin)
	{
		return false;
	}

	const bool bToIsCastObjectInput =
		Cast<UK2Node_DynamicCast>(ToNode) != nullptr &&
		ToPin->Direction == EGPD_Input &&
		ToPin->PinName.ToString().Equals(TEXT("Object"), ESearchCase::IgnoreCase);
	if (!bToIsCastObjectInput || !PinLooksLikeHitResultStruct(FromPin))
	{
		return false;
	}

	auto TryBridge = [&](UEdGraphNode* BridgeNode, UEdGraphPin* InHitPin, UEdGraphPin* OutActorPin) -> bool
	{
		if (!BridgeNode || !InHitPin || !OutActorPin)
		{
			return false;
		}

		bool bOkA = false;
		bool bOkB = false;
		if (const UEdGraphSchema* S1 = FromPin->GetSchema())
		{
			bOkA = S1->TryCreateConnection(FromPin, InHitPin);
		}
		if (bOkA)
		{
			if (UK2Node_DynamicCast* CastNode = Cast<UK2Node_DynamicCast>(ToNode))
			{
				CastNode->ReconstructNode();
				if (UEdGraphPin* RefreshedObjectPin = FindPinLoose(CastNode, TEXT("Object")))
				{
					ToPin = RefreshedObjectPin;
				}
			}
		}
		if (const UEdGraphSchema* S2 = OutActorPin->GetSchema())
		{
			bOkB = S2->TryCreateConnection(OutActorPin, ToPin);
		}

		const auto PinLinked = [](UEdGraphPin* A, UEdGraphPin* B) -> bool
		{
			if (!A || !B)
			{
				return false;
			}
			for (UEdGraphPin* Linked : A->LinkedTo)
			{
				if (Linked == B)
				{
					return true;
				}
			}
			return false;
		};
		return PinLinked(FromPin, InHitPin) && PinLinked(OutActorPin, ToPin);
	};

	if (UScriptStruct* HitStruct = FHitResult::StaticStruct())
	{
		UK2Node_BreakStruct* BreakNode = NewObject<UK2Node_BreakStruct>(Graph);
		BreakNode->StructType = HitStruct;
		Graph->AddNode(BreakNode, true, false);
		BreakNode->NodePosX = ToNode->NodePosX - 300;
		BreakNode->NodePosY = ToNode->NodePosY + 40;
		BreakNode->CreateNewGuid();
		BreakNode->AllocateDefaultPins();
		BreakNode->PostPlacedNewNode();

		UEdGraphPin* InHit = FindBreakStructInputPin(BreakNode);
		UEdGraphPin* OutHitActor = FindBreakStructHitActorPin(BreakNode);
		if (TryBridge(BreakNode, InHit, OutHitActor))
		{
			return true;
		}
		Graph->RemoveNode(BreakNode);
	}

	if (UFunction* BreakHit = FindBreakHitResultFunction())
	{
		UK2Node_CallFunction* BreakNode = NewObject<UK2Node_CallFunction>(Graph);
		BreakNode->SetFromFunction(BreakHit);
		Graph->AddNode(BreakNode, true, false);
		BreakNode->NodePosX = ToNode->NodePosX - 300;
		BreakNode->NodePosY = ToNode->NodePosY + 40;
		BreakNode->CreateNewGuid();
		BreakNode->AllocateDefaultPins();
		BreakNode->PostPlacedNewNode();

		UEdGraphPin* InHit = FindBreakHitFunctionInputPin(BreakNode);
		UEdGraphPin* OutHitActor = FindBreakHitActorOutputPin(BreakNode);
		if (TryBridge(BreakNode, InHit, OutHitActor))
		{
			return true;
		}
		Graph->RemoveNode(BreakNode);
	}

	return false;
}

static const TCHAR* SelectCasesMetaPrefix = TEXT("BPAI_CASES=");

static bool TryGetSelectCaseLabelsFromComment(const FString& Comment, TArray<FString>& OutLabels)
{
	OutLabels.Reset();
	const int32 PrefixLen = FCString::Strlen(SelectCasesMetaPrefix);
	int32 Pos = Comment.Find(SelectCasesMetaPrefix, ESearchCase::CaseSensitive);
	if (Pos == INDEX_NONE)
	{
		return false;
	}
	FString Line = Comment.Mid(Pos + PrefixLen);
	{
		const int32 LineEnd = Line.Find(TEXT("\n"), ESearchCase::CaseSensitive);
		if (LineEnd != INDEX_NONE)
		{
			Line = Line.Left(LineEnd);
		}
	}
	Line.TrimStartAndEndInline();
	if (Line.IsEmpty())
	{
		return false;
	}
	Line.ParseIntoArray(OutLabels, TEXT("|"), true);
	for (FString& L : OutLabels)
	{
		L.TrimStartAndEndInline();
	}
	return OutLabels.Num() > 0;
}

static void AppendSelectCasesMetadata(UK2Node_Select* Node, const TArray<FString>& CaseValues)
{
	if (!Node || CaseValues.Num() == 0)
	{
		return;
	}
	if (Node->NodeComment.Contains(SelectCasesMetaPrefix))
	{
		return;
	}
	Node->Modify();
	FString Line = SelectCasesMetaPrefix;
	for (int32 i = 0; i < CaseValues.Num(); ++i)
	{
		if (i > 0)
		{
			Line += TEXT("|");
		}
		Line += CaseValues[i].TrimStartAndEnd();
	}
	if (!Node->NodeComment.IsEmpty())
	{
		Node->NodeComment += LINE_TERMINATOR;
	}
	Node->NodeComment += Line;
}

/** 将 A/B/C、cases 标签或数字下标解析到 Select 的 Option N 引脚。 */
static UEdGraphPin* TryResolveSelectOptionPinBySemanticName(UK2Node_Select* Sel, const FString& RawIn)
{
	if (!Sel)
	{
		return nullptr;
	}
	const FString Raw = RawIn.TrimStartAndEnd();
	if (Raw.IsEmpty())
	{
		return nullptr;
	}

	TArray<UEdGraphPin*> OptionPins;
	Sel->GetOptionPins(OptionPins);
	if (OptionPins.Num() == 0)
	{
		return nullptr;
	}

	TArray<FString> CaseLabels;
	if (TryGetSelectCaseLabelsFromComment(Sel->NodeComment, CaseLabels))
	{
		for (int32 i = 0; i < CaseLabels.Num() && i < OptionPins.Num(); ++i)
		{
			if (CaseLabels[i].Equals(Raw, ESearchCase::IgnoreCase))
			{
				return OptionPins[i];
			}
		}
	}

	// 数字下标：0、[1]、Option2（归一化后）
	{
		FString D = Raw;
		D.ReplaceInline(TEXT("["), TEXT(""));
		D.ReplaceInline(TEXT("]"), TEXT(""));
		if (D.StartsWith(TEXT("Option"), ESearchCase::IgnoreCase))
		{
			D = D.Mid(6).TrimStartAndEnd();
		}
		D.TrimStartAndEndInline();
		D.ReplaceInline(TEXT(" "), TEXT(""));
		if (!D.IsEmpty())
		{
			bool bAllDigit = true;
			for (int32 c = 0; c < D.Len(); ++c)
			{
				if (!FChar::IsDigit(D[c]))
				{
					bAllDigit = false;
					break;
				}
			}
			if (bAllDigit)
			{
				const int32 Idx = FCString::Atoi(*D);
				if (OptionPins.IsValidIndex(Idx))
				{
					return OptionPins[Idx];
				}
			}
		}
	}

	// 无元数据时：单字母 A→第 0 项（与 A/B/C 三选项的常见 DSL 一致）
	if (Raw.Len() == 1 && FChar::IsAlpha(Raw[0]))
	{
		const int32 Idx = static_cast<int32>(FChar::ToUpper(Raw[0]) - TEXT('A'));
		if (Idx >= 0 && Idx < OptionPins.Num())
		{
			return OptionPins[Idx];
		}
	}

	return nullptr;
}

/**
 * Select 在无连线时 Option/Return/Index 均为 Wildcard；对 Wildcard 执行 TrySetDefaultValue 常表现为「步骤成功但图上无字面量」。
 * 通过 UK2Node_Select::ChangePinType 将 Return 与选项定型为 String、Index 定型为 Int（与用例 27：A/B/C + Index=2 一致）。
 */
static void EnsureSelectPinsTypedForLiteralDefaults(UK2Node_Select* Node)
{
	if (!Node)
	{
		return;
	}

	UEdGraphPin* ReturnPin = Node->GetReturnValuePin();
	if (!ReturnPin)
	{
		return;
	}

	if (ReturnPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard && Node->CanChangePinType(ReturnPin))
	{
		ReturnPin->Modify();
		ReturnPin->PinType.PinCategory = UEdGraphSchema_K2::PC_String;
		ReturnPin->PinType.PinSubCategory = NAME_None;
		ReturnPin->PinType.PinSubCategoryObject = nullptr;
		Node->ChangePinType(ReturnPin);
	}

	if (UEdGraphPin* IndexPin = Node->GetIndexPin())
	{
		if (IndexPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard && Node->CanChangePinType(IndexPin))
		{
			IndexPin->Modify();
			IndexPin->PinType.PinCategory = UEdGraphSchema_K2::PC_Int;
			IndexPin->PinType.PinSubCategory = NAME_None;
			IndexPin->PinType.PinSubCategoryObject = nullptr;
			Node->ChangePinType(IndexPin);
		}
	}

	if (UEdGraph* G = Node->GetGraph())
	{
		G->NotifyNodeChanged(Node);
	}
}

/** Set 节点：在未连线时写入变量值引脚的字面量（如 Mode=1）。 */
static void TryApplyVariableSetLiteralValue(UK2Node_VariableSet* Node, const FString& VarName, const FString& Literal)
{
	if (!Node || Literal.TrimStartAndEnd().IsEmpty())
	{
		return;
	}
	const FString Lit = Literal.TrimStartAndEnd();

	UEdGraphPin* ValPin = FindPinLoose(Node, VarName);
	if (!ValPin)
	{
		ValPin = FindPinLoose(Node, TEXT("Value"));
	}
	if (!ValPin || ValPin->Direction != EGPD_Input || ValPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec)
	{
		for (UEdGraphPin* P : Node->Pins)
		{
			if (P && P->Direction == EGPD_Input && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
			{
				ValPin = P;
				break;
			}
		}
	}
	if (!ValPin || ValPin->LinkedTo.Num() > 0)
	{
		return;
	}

	if (const UEdGraphSchema* Schema = ValPin->GetSchema())
	{
		Schema->TrySetDefaultValue(*ValPin, Lit);
		if (UEdGraphNode* Owning = ValPin->GetOwningNode())
		{
			Owning->PinDefaultValueChanged(ValPin);
		}
	}
}

