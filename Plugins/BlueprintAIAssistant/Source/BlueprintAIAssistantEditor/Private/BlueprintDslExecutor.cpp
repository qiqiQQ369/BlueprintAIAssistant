static FObjectProperty* FindBestPrimitiveComponentProperty(UBlueprint* Blueprint)
{
	if (!Blueprint || !Blueprint->SkeletonGeneratedClass)
	{
		return nullptr;
	}

	auto Score = [](const FObjectProperty* P) -> int32
	{
		if (!P || !P->PropertyClass)
		{
			return 0;
		}
		if (!P->PropertyClass->IsChildOf(UPrimitiveComponent::StaticClass()))
		{
			return 0;
		}
		const FString N = P->GetName().ToLower();
		const FString C = P->PropertyClass->GetName().ToLower();
		int32 S = 10;
		if (N.Contains(TEXT("capsule")) || C.Contains(TEXT("capsule"))) S += 50;
		if (N.Contains(TEXT("sphere")) || C.Contains(TEXT("sphere"))) S += 40;
		if (N.Contains(TEXT("box")) || C.Contains(TEXT("box"))) S += 35;
		if (N.Contains(TEXT("collision")) || C.Contains(TEXT("collision"))) S += 20;
		return S;
	};

	FObjectProperty* Best = nullptr;
	int32 BestScore = 0;
	for (TFieldIterator<FObjectProperty> It(Blueprint->SkeletonGeneratedClass, EFieldIteratorFlags::IncludeSuper); It; ++It)
	{
		FObjectProperty* P = *It;
		const int32 S = Score(P);
		if (S > BestScore)
		{
			BestScore = S;
			Best = P;
		}
	}
	return Best;
}

/** 金币/拾取物等 Actor 蓝图上若无碰撞体，自动补一个 Sphere 以便绑定 OnComponentBeginOverlap。 */
static bool EnsureDefaultOverlapPrimitiveComponent(UBlueprint* Blueprint)
{
	if (!Blueprint)
	{
		return false;
	}
	if (FindBestPrimitiveComponentProperty(Blueprint))
	{
		return true;
	}

	USimpleConstructionScript* SCS = Blueprint->SimpleConstructionScript;
	if (!SCS)
	{
		return false;
	}

	USCS_Node* NewNode = SCS->CreateNode(USphereComponent::StaticClass(), TEXT("PickUpOverlap"));
	if (!NewNode)
	{
		return false;
	}

	if (USphereComponent* SphereTemplate = Cast<USphereComponent>(NewNode->ComponentTemplate))
	{
		SphereTemplate->SetRelativeLocation(FVector::ZeroVector);
		SphereTemplate->SetSphereRadius(50.f);
	}

	// 不要挂到 DefaultSceneRoot 下（会触发 SCS 循环层级 ensure）；优先挂到真实根组件，否则作为根节点加入。
	USCS_Node* AttachParent = nullptr;
	for (USCS_Node* Root : SCS->GetRootNodes())
	{
		if (Root && Root != SCS->GetDefaultSceneRootNode())
		{
			AttachParent = Root;
			break;
		}
	}
	if (AttachParent)
	{
		AttachParent->AddChildNode(NewNode);
		NewNode->SetParent(AttachParent);
	}
	else
	{
		SCS->AddNode(NewNode);
	}

	FBlueprintEditorUtils::MarkBlueprintAsStructurallyModified(Blueprint);
	FKismetEditorUtilities::CompileBlueprint(Blueprint);
	return FindBestPrimitiveComponentProperty(Blueprint) != nullptr;
}

