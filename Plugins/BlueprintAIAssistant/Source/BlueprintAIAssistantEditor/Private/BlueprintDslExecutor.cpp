/** MakeArrayInt：手动锁 int 数组类型并同步元素 pin（勿对 Output 调 NotifyPinConnectionListChanged：无连线时引擎会把输出打回 wildcard）。 */
static void LockMakeArrayIntTypes(UK2Node_MakeArray* Node)
{
	if (!Node)
	{
		return;
	}

	UEdGraphPin* Out = Node->GetOutputPin();
	if (!Out)
	{
		return;
	}

	const UEdGraphSchema_K2* SchemaK2 = GetDefault<UEdGraphSchema_K2>();

	Out->Modify();
	Out->PinType.PinCategory = UEdGraphSchema_K2::PC_Int;
	Out->PinType.PinSubCategory = NAME_None;
	Out->PinType.PinSubCategoryObject = nullptr;
	Out->PinType.ContainerType = EPinContainerType::Array;

	// 与 UK2Node_MakeContainer::PropagatePinType 对「键」输入的处理一致：同步元素类别（不复制 Array 容器到元素 pin）
	for (UEdGraphPin* P : Node->Pins)
	{
		if (P && P->Direction == EGPD_Input && P->ParentPin == nullptr)
		{
			P->Modify();
			P->PinType.PinCategory = UEdGraphSchema_K2::PC_Int;
			P->PinType.PinSubCategory = NAME_None;
			P->PinType.PinSubCategoryObject = nullptr;
			P->PinType.ContainerType = EPinContainerType::None;
			SchemaK2->SetPinAutogeneratedDefaultValueBasedOnType(P);
		}
	}

	if (UEdGraph* Graph = Node->GetGraph())
	{
		Graph->NotifyNodeChanged(Node);
	}
}

