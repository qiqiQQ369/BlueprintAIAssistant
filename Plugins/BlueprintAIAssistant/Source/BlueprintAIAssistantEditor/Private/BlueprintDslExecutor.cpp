static FString NormalizePinKey(const FString& In)
{
	FString S = In.TrimStartAndEnd();
	S.ReplaceInline(TEXT("\r"), TEXT(""));
	S.ReplaceInline(TEXT("\n"), TEXT(""));
	// 统一空格/下划线，尽量贴合常见 K2Node 的内部 PinName 规则
	S.ReplaceInline(TEXT(" "), TEXT("_"));
	// MakeArray 等节点的元素 pin 有时会显示为 "[0]" / "[1]"，这里去掉括号做归一化
	S.ReplaceInline(TEXT("["), TEXT(""));
	S.ReplaceInline(TEXT("]"), TEXT(""));
	return S;
}

static FCriticalSection GPinAliasMutex;
static TMap<FString, FString> GUserPinAlias; // from -> to
static bool GUserPinAliasLoaded = false;

static FString GetUserPinAliasFilePath()
{
	return FPaths::ConvertRelativePathToFull(FPaths::ProjectSavedDir() / TEXT("BlueprintAIAssistant") / TEXT("pin-aliases.json"));
}

static void LoadUserPinAliasesFromDisk_NoLock()
{
	GUserPinAlias.Reset();
	GUserPinAliasLoaded = true;

	const FString FilePath = GetUserPinAliasFilePath();
	FString Content;
	if (!FFileHelper::LoadFileToString(Content, *FilePath) || Content.IsEmpty())
	{
		return;
	}

	TSharedPtr<FJsonObject> Root;
	const TSharedRef<TJsonReader<TCHAR>> Reader = TJsonReaderFactory<TCHAR>::Create(Content);
	if (!FJsonSerializer::Deserialize(Reader, Root) || !Root.IsValid())
	{
		return;
	}

	const TArray<TSharedPtr<FJsonValue>>* Aliases = nullptr;
	if (!Root->TryGetArrayField(TEXT("aliases"), Aliases) || !Aliases)
	{
		return;
	}

	for (const TSharedPtr<FJsonValue>& V : *Aliases)
	{
		const TSharedPtr<FJsonObject>* Obj = nullptr;
		if (!V.IsValid() || !V->TryGetObject(Obj) || !Obj || !Obj->IsValid())
		{
			continue;
		}

		FString From;
		FString To;
		(*Obj)->TryGetStringField(TEXT("from"), From);
		(*Obj)->TryGetStringField(TEXT("to"), To);
		From = From.TrimStartAndEnd();
		To = To.TrimStartAndEnd();
		if (From.IsEmpty() || To.IsEmpty())
		{
			continue;
		}

		GUserPinAlias.Add(From, To);
		const FString Norm = NormalizePinKey(From);
		if (!Norm.IsEmpty() && !Norm.Equals(From, ESearchCase::CaseSensitive))
		{
			GUserPinAlias.Add(Norm, To);
		}
	}
}

// 是否看起来像"输入执行"别名（Exec / In / Execute / InExec ...）
static bool IsInputExecAlias(const FString& Name)
{
	const FString S = Name.TrimStartAndEnd();
	// 与 UEdGraphSchema_K2::PN_Execute（"execute"）对齐，LLM 常写小写 execute
	return S.Equals(UEdGraphSchema_K2::PN_Execute.ToString(), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Exec"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Execute"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("In"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("InExec"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Input"), ESearchCase::IgnoreCase);
}

// 是否看起来像"输出执行"别名（Then / Out / OutExec / Completed ...）
static bool IsOutputExecAlias(const FString& Name)
{
	const FString S = Name.TrimStartAndEnd();
	return S.Equals(TEXT("Then"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Out"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("OutExec"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Completed"), ESearchCase::IgnoreCase) ||
	       S.Equals(TEXT("Output"), ESearchCase::IgnoreCase);
}

// 在节点上找指定方向的首个 Exec pin。Spawn/Delay 这种只有一根输出 exec 的情形很常见，这里直接返回第一根。
static UEdGraphPin* FindFirstExecPinByDirection(UEdGraphNode* Node, EEdGraphPinDirection Dir)
{
	if (!Node)
	{
		return nullptr;
	}
	for (UEdGraphPin* P : Node->Pins)
	{
		if (P && P->Direction == Dir && P->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec)
		{
			return P;
		}
	}
	return nullptr;
}

// LLM 常见的 pin 名误写 → 真实 UFUNCTION 参数名（即内部 PinName）映射表。
// 只对无歧义的常见错误生效，避免误匹配。
static FString ResolvePinNameAlias(const FString& Raw)
{
	{
		FScopeLock Lock(&GPinAliasMutex);
		if (!GUserPinAliasLoaded)
		{
			LoadUserPinAliasesFromDisk_NoLock();
		}
		const FString TrimmedKey = Raw.TrimStartAndEnd();
		if (const FString* M = GUserPinAlias.Find(TrimmedKey))
		{
			return *M;
		}
		const FString NormKey = NormalizePinKey(TrimmedKey);
		if (!NormKey.IsEmpty())
		{
			if (const FString* M2 = GUserPinAlias.Find(NormKey))
			{
				return *M2;
			}
		}
	}

	static const TMap<FString, FString> Alias = {
		// PlayAnimMontage（ACharacter）：参数名 AnimMontage / InPlayRate / StartSectionName
		{ TEXT("MontageToPlay"),        TEXT("AnimMontage") },
		{ TEXT("Montage"),              TEXT("AnimMontage") },
		{ TEXT("PlayRate"),             TEXT("InPlayRate") },

		// PrintString（UKismetSystemLibrary::PrintString）：参数 InString / bPrintToScreen / bPrintToLog / TextColor / Duration
		{ TEXT("String"),               TEXT("InString") },
		{ TEXT("Text"),                 TEXT("InString") },
		{ TEXT("Message"),              TEXT("InString") },

		// Conv_FloatToString / Conv_DoubleToString 在不同引擎函数签名下输入 pin 可能为 InFloat / InDouble
		{ TEXT("In Float"),             TEXT("InDouble") },
		{ TEXT("InFloat"),              TEXT("InDouble") },
		{ TEXT("In Double"),            TEXT("InFloat") },
		{ TEXT("InDouble"),             TEXT("InFloat") },

		// SpawnActorFromClass：内部 pin 名为 Class / SpawnTransform / CollisionHandlingOverride
		{ TEXT("SpawnClass"),           TEXT("Class") },
		{ TEXT("ActorClass"),           TEXT("Class") },
		{ TEXT("Transform"),            TEXT("SpawnTransform") },

		// BreakVector/BreakRotator 已改为 KismetMathLibrary::BreakVector（引脚 InVec），勿再把 InVec 映射成 Vector。
	};

	const FString Trimmed = Raw.TrimStartAndEnd();
	if (const FString* Mapped = Alias.Find(Trimmed))
	{
		return *Mapped;
	}
	return Trimmed;
}

static UEdGraphPin* FindPinLoose(UEdGraphNode* Node, const FString& PinNameOrDisplay)
{
	if (!Node)
	{
		return nullptr;
	}

	const FString Raw = PinNameOrDisplay.TrimStartAndEnd();
	if (Raw.IsEmpty())
	{
		return nullptr;
	}

	if (Raw.Equals(TEXT("self"), ESearchCase::IgnoreCase))
	{
		if (UEdGraphPin* P = Node->FindPin(UEdGraphSchema_K2::PN_Self))
		{
			return P;
		}
	}

	// 特判：DynamicCast 节点在不同语言环境下 pin 显示名可能是本地化文本，
	// 但内部 PinName 通常仍是 then / CastFailed / Object / AsXXX。
	// LLM 常写 "Cast Succeeded/Failed"，这里直接映射到标准 pin。
	if (Cast<UK2Node_DynamicCast>(Node) != nullptr)
	{
		if (Raw.Equals(TEXT("Cast Succeeded"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("CastSucceeded"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("Succeeded"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("Success"), ESearchCase::IgnoreCase))
		{
			if (UEdGraphPin* P = Node->FindPin(UEdGraphSchema_K2::PN_Then))
			{
				return P;
			}
		}
		if (Raw.Equals(TEXT("Cast Failed"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("CastFailed"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("Failed"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("Fail"), ESearchCase::IgnoreCase))
		{
			if (UEdGraphPin* P = Node->FindPin(FName(TEXT("CastFailed"))))
			{
				return P;
			}
		}
	}

	// 0) 常见别名映射（MontageToPlay → AnimMontage 等）
	const FString Aliased = ResolvePinNameAlias(Raw);
	if (!Aliased.Equals(Raw, ESearchCase::CaseSensitive))
	{
		if (UEdGraphPin* P = Node->FindPin(FName(*Aliased)))
		{
			return P;
		}
	}

	// 1) 精确按内部 PinName
	if (UEdGraphPin* P = Node->FindPin(FName(*Raw)))
	{
		return P;
	}

	// 2) 空格/下划线归一化再试
	const FString Norm = NormalizePinKey(Raw);
	if (!Norm.IsEmpty())
	{
		if (UEdGraphPin* P = Node->FindPin(FName(*Norm)))
		{
			return P;
		}
	}

	// 3) 遍历 Pins，比对内部名/显示名（忽略大小写，且做归一化）
	for (UEdGraphPin* P : Node->Pins)
	{
		if (!P)
		{
			continue;
		}

		const FString Internal = P->PinName.ToString();
		const FString InternalNorm = NormalizePinKey(Internal);
		if (Internal.Equals(Raw, ESearchCase::IgnoreCase) || InternalNorm.Equals(Norm, ESearchCase::IgnoreCase))
		{
			return P;
		}

		const FString Display = P->GetDisplayName().ToString();
		const FString DisplayNorm = NormalizePinKey(Display);
		if (Display.Equals(Raw, ESearchCase::IgnoreCase) || DisplayNorm.Equals(Norm, ESearchCase::IgnoreCase))
		{
			return P;
		}
	}

	// 4) Exec pin 别名兜底：LLM 常写 "Exec" / "Then" / "Out"，而真实 PinName 是
	//    UEdGraphSchema_K2::PN_Execute = "execute"、PN_Then = "then"。
	//    由于这里不知道"期望方向"，我们按惯例用 Execute/Then 的标准名去拿。
	if (IsInputExecAlias(Raw))
	{
		if (UEdGraphPin* P = Node->FindPin(UEdGraphSchema_K2::PN_Execute))
		{
			return P;
		}
		if (UEdGraphPin* P = FindFirstExecPinByDirection(Node, EGPD_Input))
		{
			return P;
		}
	}
	if (IsOutputExecAlias(Raw))
	{
		if (UEdGraphPin* P = Node->FindPin(UEdGraphSchema_K2::PN_Then))
		{
			return P;
		}
		if (UEdGraphPin* P = FindFirstExecPinByDirection(Node, EGPD_Output))
		{
			return P;
		}
	}

	// 5) MakeStruct（若仍使用泛型 Make Struct）：主输出 pin 名为 Vector、Rotator 等，不是 CallFunction 的 ReturnValue。
	if (Raw.Equals(TEXT("ReturnValue"), ESearchCase::IgnoreCase))
	{
		if (Cast<UK2Node_MakeStruct>(Node))
		{
			static const TCHAR* PreferredMakeOutputs[] =
			{
				TEXT("Vector"), TEXT("Rotator"), TEXT("Quat"), TEXT("Transform"),
				TEXT("LinearColor"), TEXT("Color"), TEXT("IntPoint"), TEXT("Guid"),
			};
			for (const TCHAR* Name : PreferredMakeOutputs)
			{
				if (UEdGraphPin* P = Node->FindPin(FName(Name)))
				{
					if (P->Direction == EGPD_Output && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
					{
						return P;
					}
				}
			}
			for (UEdGraphPin* P : Node->Pins)
			{
				if (P && P->Direction == EGPD_Output && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
				{
					return P;
				}
			}
		}
	}

	// 6) KismetMathLibrary::MakeVector/BreakVector/MakeRotator/BreakRotator（专用节点，避免泛型 Make/Break Struct 编译警告）
	if (UK2Node_CallFunction* CF = Cast<UK2Node_CallFunction>(Node))
	{
		const UFunction* TF = CF->GetTargetFunction();
		if (TF)
		{
			const FName Fn = TF->GetFName();
			if (Fn == TEXT("MakeVector") || Fn == TEXT("MakeRotator"))
			{
				// 输出 FVector/FRotator 的主引脚为 ReturnValue；LLM 有时写 Vector / Rotator
				if ((Raw.Equals(TEXT("Vector"), ESearchCase::IgnoreCase) && Fn == TEXT("MakeVector")) ||
					(Raw.Equals(TEXT("Rotator"), ESearchCase::IgnoreCase) && Fn == TEXT("MakeRotator")))
				{
					if (UEdGraphPin* P = Node->FindPin(TEXT("ReturnValue")))
					{
						return P;
					}
				}
			}
			if (Fn == TEXT("BreakVector"))
			{
				// 输入参数名 InVec；旧 DSL 或文档写 Vector
				if (Raw.Equals(TEXT("Vector"), ESearchCase::IgnoreCase))
				{
					if (UEdGraphPin* P = Node->FindPin(TEXT("InVec")))
					{
						return P;
					}
				}
			}
			if (Fn == TEXT("BreakRotator"))
			{
				if (Raw.Equals(TEXT("Rotator"), ESearchCase::IgnoreCase) || Raw.Equals(TEXT("Rotation"), ESearchCase::IgnoreCase))
				{
					if (UEdGraphPin* P = Node->FindPin(TEXT("InRot")))
					{
						return P;
					}
				}
			}
		}
	}

	// Select：模型常把 cases 里的「选项标签」（A/B/C）误当成 PinName；真实引脚名为 Option 0、Option 1…
	if (UK2Node_Select* SelNode = Cast<UK2Node_Select>(Node))
	{
		if (UEdGraphPin* P = TryResolveSelectOptionPinBySemanticName(SelNode, Raw))
		{
			return P;
		}
	}

	// 7) Real/Float 引脚名互认：InFloat ↔ InDouble；数学节点 A/B 兜底
	{
		const FString RawNorm = NormalizePinKey(Raw);
		const bool bWantsRealLike =
			RawNorm.Equals(TEXT("InFloat"), ESearchCase::IgnoreCase) ||
			RawNorm.Equals(TEXT("InDouble"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("A"), ESearchCase::IgnoreCase) ||
			Raw.Equals(TEXT("B"), ESearchCase::IgnoreCase);
		if (bWantsRealLike)
		{
			static const TCHAR* RealAlternates[] =
			{
				TEXT("InFloat"), TEXT("InDouble"), TEXT("A"), TEXT("B"),
			};
			for (const TCHAR* Alt : RealAlternates)
			{
				if (RawNorm.Equals(NormalizePinKey(Alt), ESearchCase::IgnoreCase))
				{
					continue;
				}
				if (UEdGraphPin* P = Node->FindPin(FName(Alt)))
				{
					if (P->Direction == EGPD_Input &&
						(P->PinType.PinCategory == UEdGraphSchema_K2::PC_Real ||
						 P->PinType.PinCategory == UEdGraphSchema_K2::PC_Float ||
						 P->PinType.PinCategory == UEdGraphSchema_K2::PC_Double))
					{
						return P;
					}
				}
			}
		}
	}

	return nullptr;
}

static bool PinCategoryIsNumericOrBoolForToString(const FEdGraphPinType& PinType)
{
	const FName Cat = PinType.PinCategory;
	return Cat == UEdGraphSchema_K2::PC_Int ||
		Cat == UEdGraphSchema_K2::PC_Int64 ||
		Cat == UEdGraphSchema_K2::PC_Real ||
		Cat == UEdGraphSchema_K2::PC_Float ||
		Cat == UEdGraphSchema_K2::PC_Double ||
		Cat == UEdGraphSchema_K2::PC_Boolean ||
		Cat == UEdGraphSchema_K2::PC_Name;
}

static UFunction* FindKismetConvToStringFunction(const FEdGraphPinType& FromType)
{
	const FName Cat = FromType.PinCategory;
	if (Cat == UEdGraphSchema_K2::PC_Int || Cat == UEdGraphSchema_K2::PC_Int64)
	{
		return UKismetStringLibrary::StaticClass()->FindFunctionByName(TEXT("Conv_IntToString"));
	}
	if (Cat == UEdGraphSchema_K2::PC_Real || Cat == UEdGraphSchema_K2::PC_Float || Cat == UEdGraphSchema_K2::PC_Double)
	{
		if (FromType.PinSubCategory == UEdGraphSchema_K2::PC_Double)
		{
			if (UFunction* Fn = UKismetStringLibrary::StaticClass()->FindFunctionByName(TEXT("Conv_DoubleToString")))
			{
				return Fn;
			}
		}
		return UKismetStringLibrary::StaticClass()->FindFunctionByName(TEXT("Conv_FloatToString"));
	}
	if (Cat == UEdGraphSchema_K2::PC_Boolean)
	{
		return UKismetStringLibrary::StaticClass()->FindFunctionByName(TEXT("Conv_BoolToString"));
	}
	if (Cat == UEdGraphSchema_K2::PC_Name)
	{
		return UKismetStringLibrary::StaticClass()->FindFunctionByName(TEXT("Conv_NameToString"));
	}
	return nullptr;
}

static UEdGraphPin* FindConvFunctionInputPin(UK2Node_CallFunction* ConvNode)
{
	if (!ConvNode)
	{
		return nullptr;
	}

	static const TCHAR* Preferred[] =
	{
		TEXT("InFloat"), TEXT("InDouble"), TEXT("InInt"), TEXT("InBool"), TEXT("InName"), TEXT("A"),
	};
	for (const TCHAR* Name : Preferred)
	{
		if (UEdGraphPin* P = FindPinLoose(ConvNode, Name))
		{
			return P;
		}
	}

	for (UEdGraphPin* P : ConvNode->Pins)
	{
		if (P && P->Direction == EGPD_Input && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
		{
			return P;
		}
	}
	return nullptr;
}

static bool PinAcceptsAutoToStringInput(UEdGraphPin* ToPin, UEdGraphNode* ToNode)
{
	if (!ToPin || ToPin->PinType.PinCategory != UEdGraphSchema_K2::PC_String)
	{
		return false;
	}

	if (ToPin->PinName.ToString().Equals(TEXT("InString"), ESearchCase::IgnoreCase))
	{
		return true;
	}

	if (const UK2Node_CallFunction* CF = Cast<UK2Node_CallFunction>(ToNode))
	{
		if (const UFunction* Fn = CF->GetTargetFunction())
		{
			if (Fn->GetName().Contains(TEXT("PrintString"), ESearchCase::IgnoreCase))
			{
				return true;
			}
		}
	}
	return false;
}

static bool TryAutoInsertConvToString(
	UEdGraph* Graph,
	UEdGraphPin* FromPin,
	UEdGraphNode* ToNode,
	UEdGraphPin* ToPin)
{
	if (!Graph || !FromPin || !ToPin || !ToNode)
	{
		return false;
	}
	if (FromPin->PinType.PinCategory == UEdGraphSchema_K2::PC_String ||
		FromPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Text)
	{
		return false;
	}
	if (!PinCategoryIsNumericOrBoolForToString(FromPin->PinType))
	{
		return false;
	}
	if (!PinAcceptsAutoToStringInput(ToPin, ToNode))
	{
		return false;
	}

	UFunction* ConvFunc = FindKismetConvToStringFunction(FromPin->PinType);
	if (!ConvFunc)
	{
		return false;
	}

	UK2Node_CallFunction* ConvNode = NewObject<UK2Node_CallFunction>(Graph);
	ConvNode->SetFromFunction(ConvFunc);
	Graph->AddNode(ConvNode, true, false);
	ConvNode->NodePosX = ToNode->NodePosX - 260;
	ConvNode->NodePosY = ToNode->NodePosY + 60;
	ConvNode->CreateNewGuid();
	ConvNode->AllocateDefaultPins();
	ConvNode->PostPlacedNewNode();

	UEdGraphPin* InPin = FindConvFunctionInputPin(ConvNode);
	UEdGraphPin* OutStr = FindPinLoose(ConvNode, TEXT("ReturnValue"));
	if (!InPin || !OutStr)
	{
		return false;
	}

	if (const UEdGraphSchema* S1 = FromPin->GetSchema())
	{
		S1->TryCreateConnection(FromPin, InPin);
	}
	if (const UEdGraphSchema* S2 = OutStr->GetSchema())
	{
		return S2->TryCreateConnection(OutStr, ToPin);
	}
	return false;
}

static bool PinLooksLikeHitResultStruct(const UEdGraphPin* Pin)
{
	if (!Pin || Pin->Direction != EGPD_Output)
	{
		return false;
	}

	if (Pin->PinName.ToString().Equals(TEXT("OutHit"), ESearchCase::IgnoreCase))
	{
		return Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Struct ||
			Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard;
	}

	if (Pin->PinType.PinCategory != UEdGraphSchema_K2::PC_Struct)
	{
		return false;
	}

	if (Pin->PinType.PinSubCategory == FName(TEXT("HitResult")))
	{
		return true;
	}

	if (Pin->PinType.PinSubCategoryObject.IsValid())
	{
		const FString StructName = Pin->PinType.PinSubCategoryObject->GetName();
		if (StructName.Contains(TEXT("HitResult"), ESearchCase::IgnoreCase))
		{
			return true;
		}
	}
	return false;
}

