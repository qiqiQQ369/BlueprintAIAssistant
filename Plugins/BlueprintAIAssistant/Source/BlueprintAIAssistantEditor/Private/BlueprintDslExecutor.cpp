static void TagNodeWithNodeId(UEdGraphNode* Node, const FString& NodeId)
{
	if (!Node || NodeId.IsEmpty())
	{
		return;
	}

	const FString Tag = BuildNodeIdTag(NodeId);
	if (!Node->NodeComment.Contains(Tag))
	{
		if (!Node->NodeComment.IsEmpty())
		{
			Node->NodeComment += TEXT("\n");
		}
		Node->NodeComment += Tag;
	}
}

static UClass* InferCastTargetClassFromHints(const FString& NodeId, const FString& TargetClassField, const FString& FunctionNameField)
{
	FString Explicit = TargetClassField.TrimStartAndEnd();
	if (Explicit.IsEmpty())
	{
		Explicit = FunctionNameField.TrimStartAndEnd();
	}
	if (!Explicit.IsEmpty())
	{
		if (UClass* Resolved = ResolveClassByName(Explicit))
		{
			return Resolved;
		}
		if (UClass* Loose = ResolveClassByLooseName(Explicit))
		{
			return Loose;
		}
	}

	const FString Id = NodeId.ToLower();
	if (Id.Contains(TEXT("character")) || Id.Contains(TEXT("player")))
	{
		return ACharacter::StaticClass();
	}
	if (Id.Contains(TEXT("pawn")))
	{
		return APawn::StaticClass();
	}
	if (Id.Contains(TEXT("actor")))
	{
		return AActor::StaticClass();
	}
	return nullptr;
}

static void TagAutoCreatedEventNode(UEdGraphNode* Node, const FString& SemanticNodeId)
{
	if (Node && !SemanticNodeId.IsEmpty())
	{
		TagNodeWithNodeId(Node, SemanticNodeId);
	}
}

