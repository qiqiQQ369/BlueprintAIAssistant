static FString NormalizeLooseClassName(const FString& InName)
{
	FString S = InName.TrimStartAndEnd();
	if (S.StartsWith(TEXT("U")) || S.StartsWith(TEXT("A")) || S.StartsWith(TEXT("F")))
	{
		S = S.Mid(1);
	}
	return S;
}

static bool TrySplitQualifiedFunctionName(const FString& InFuncName, FString& OutOwnerName, FString& OutFunctionName)
{
	OutOwnerName.Reset();
	OutFunctionName = InFuncName.TrimStartAndEnd();

	const int32 ScopePos = OutFunctionName.Find(TEXT("::"), ESearchCase::CaseSensitive);
	if (ScopePos == INDEX_NONE)
	{
		return false;
	}

	OutOwnerName = OutFunctionName.Left(ScopePos).TrimStartAndEnd();
	OutFunctionName = OutFunctionName.Mid(ScopePos + 2).TrimStartAndEnd();
	return !OutOwnerName.IsEmpty() && !OutFunctionName.IsEmpty();
}

static FString NormalizeMathSemanticKey(const FString& InRaw)
{
	FString S = InRaw.TrimStartAndEnd().ToLower();
	S.ReplaceInline(TEXT("`"), TEXT(""));
	S.ReplaceInline(TEXT(" "), TEXT(""));
	S.ReplaceInline(TEXT("_"), TEXT(""));
	S.ReplaceInline(TEXT("("), TEXT(""));
	S.ReplaceInline(TEXT(")"), TEXT(""));
	S.ReplaceInline(TEXT("["), TEXT(""));
	S.ReplaceInline(TEXT("]"), TEXT(""));
	S.ReplaceInline(TEXT("＋"), TEXT("+"));
	S.ReplaceInline(TEXT("×"), TEXT("*"));
	return S;
}

static void AddUniqueString(TArray<FString>& Arr, const FString& Value)
{
	const FString V = Value.TrimStartAndEnd();
	if (V.IsEmpty())
	{
		return;
	}
	for (const FString& E : Arr)
	{
		if (E.Equals(V, ESearchCase::IgnoreCase))
		{
			return;
		}
	}
	Arr.Add(V);
}

