static FString TrimInputKeyToken(const FString& In)
{
	FString S = In.TrimStartAndEnd();
	while (!S.IsEmpty())
	{
		const TCHAR C = S[0];
		if (C == TEXT('_') || C == TEXT('-') || C == TEXT(':') || C == TEXT(' ') || C == TEXT('(') || C == TEXT('['))
		{
			S.RightChopInline(1, EAllowShrinking::No);
			S.TrimStartInline();
			continue;
		}
		break;
	}
	while (!S.IsEmpty())
	{
		const TCHAR C = S[S.Len() - 1];
		if (C == TEXT('_') || C == TEXT('-') || C == TEXT(':') || C == TEXT(' ') || C == TEXT(')') || C == TEXT(']'))
		{
			S.LeftChopInline(1, EAllowShrinking::No);
			S.TrimEndInline();
			continue;
		}
		break;
	}
	return S.TrimStartAndEnd();
}

static bool TryResolveInputKeyName(const FString& InRaw, FKey& OutKey)
{
	const FString Raw = TrimInputKeyToken(InRaw);
	if (Raw.IsEmpty())
	{
		return false;
	}

	TArray<FKey> AllKeys;
	EKeys::GetAllKeys(AllKeys);
	for (const FKey& Key : AllKeys)
	{
		if (!Key.IsValid())
		{
			continue;
		}

		if (Key.GetFName().ToString().Equals(Raw, ESearchCase::IgnoreCase) ||
			Key.GetDisplayName().ToString().Equals(Raw, ESearchCase::IgnoreCase))
		{
			OutKey = Key;
			return true;
		}
	}

	return false;
}

static bool TryExtractInputKeyHint(const FString& InRaw, FString& OutHint)
{
	OutHint.Reset();

	const FString Raw = InRaw.TrimStartAndEnd();
	if (Raw.IsEmpty())
	{
		return false;
	}

	auto TakeSuffixAfterPrefix = [&](const TCHAR* Prefix) -> bool
	{
		if (!Raw.StartsWith(Prefix, ESearchCase::IgnoreCase))
		{
			return false;
		}
		const FString Hint = TrimInputKeyToken(Raw.Mid(FCString::Strlen(Prefix)));
		if (Hint.IsEmpty())
		{
			return false;
		}
		OutHint = Hint;
		return true;
	};

	if (TakeSuffixAfterPrefix(TEXT("EventInputKey")) ||
		TakeSuffixAfterPrefix(TEXT("EventInputAction")) ||
		TakeSuffixAfterPrefix(TEXT("InputAction")) ||
		TakeSuffixAfterPrefix(TEXT("InputKey")) ||
		TakeSuffixAfterPrefix(TEXT("Keyboard")) ||
		TakeSuffixAfterPrefix(TEXT("Key")))
	{
		return true;
	}

	// "Input Action E" → compact → InputActionE
	FString Compact = Raw;
	Compact.ReplaceInline(TEXT(" "), TEXT(""));
	if (Compact != Raw)
	{
		FString CompactHint;
		if (TryExtractInputKeyHint(Compact, CompactHint))
		{
			OutHint = CompactHint;
			return true;
		}
	}

	if (Raw.EndsWith(TEXT("Key"), ESearchCase::IgnoreCase) && Raw.Len() > 3)
	{
		const FString Hint = TrimInputKeyToken(Raw.LeftChop(3));
		if (!Hint.IsEmpty())
		{
			OutHint = Hint;
			return true;
		}
	}

	return false;
}

static bool TryResolveInputKeyFromDslToken(const FString& InRaw, FKey& OutKey)
{
	if (TryResolveInputKeyName(InRaw, OutKey))
	{
		return true;
	}

	FString Hint;
	if (TryExtractInputKeyHint(InRaw, Hint))
	{
		return TryResolveInputKeyName(Hint, OutKey);
	}

	return false;
}

static bool TryResolveInputKeyForCreateStep(const FBlueprintDslActionStep& Step, FKey& OutKey)
{
	return TryResolveInputKeyFromDslToken(Step.FunctionName, OutKey) ||
		TryResolveInputKeyFromDslToken(Step.TargetClass, OutKey) ||
		TryResolveInputKeyFromDslToken(Step.NodeId, OutKey);
}

