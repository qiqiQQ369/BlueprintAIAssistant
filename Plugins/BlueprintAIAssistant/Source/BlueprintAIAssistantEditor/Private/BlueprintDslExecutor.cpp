static bool ExecuteSetVariableAction(const FBlueprintDslActionStep& Step, UEdGraph* Graph, FString& OutError)
{
	if (!Graph)
	{
		OutError = TEXT("目标图为空。");
		return false;
	}
	if (Step.NodeId.IsEmpty())
	{
		OutError = TEXT("SetVariable.nodeId 为空。");
		return false;
	}
	if (Step.VarName.IsEmpty())
	{
		OutError = TEXT("SetVariable.varName 为空。");
		return false;
	}

	UEdGraphNode* Existing = FindNodeByTaggedNodeId(Graph, Step.NodeId);
	UK2Node_VariableSet* Node = Existing ? Cast<UK2Node_VariableSet>(Existing) : nullptr;
	if (!Node)
	{
		// 简单布局：按节点数量右移
		int32 PosX = 200;
		int32 PosY = 200;
		{
			int32 Count = 0;
			for (UEdGraphNode* N : Graph->Nodes) { if (N) ++Count; }
			PosX += Count * 40;
			PosY += Count * 20;
		}

		Node = NewObject<UK2Node_VariableSet>(Graph);
		Node->VariableReference.SetSelfMember(FName(*Step.VarName));
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();

		TagNodeWithNodeId(Node, Step.NodeId);
		Node->SnapToGrid(16);
	}

	// v2: valueFrom 自动连到 Set 节点的 Value pin（若提供）
	if (!Step.ValueFromNodeId.IsEmpty() && !Step.ValueFromPin.IsEmpty())
	{
		UEdGraphNode* FromNode = FindNodeByTaggedNodeId(Graph, Step.ValueFromNodeId);
		if (!FromNode)
		{
			OutError = FString::Printf(TEXT("SetVariable.valueFromNodeId=%s 未找到。"), *Step.ValueFromNodeId);
			return false;
		}
		UEdGraphPin* FromPin = FindPinLoose(FromNode, Step.ValueFromPin);
		if (!FromPin)
		{
			OutError = FString::Printf(TEXT("SetVariable.valueFromPin=%s 未找到。\n可用 Pins:\n%s"), *Step.ValueFromPin, *DumpNodePinsForDebug(FromNode));
			return false;
		}

		UEdGraphPin* ToPin = Node->FindPin(FName(*Step.VarName));
		if (!ToPin)
		{
			ToPin = FindPinLoose(Node, TEXT("Value"));
		}
		if (!ToPin)
		{
			for (UEdGraphPin* P : Node->Pins)
			{
				if (P && P->Direction == EGPD_Input && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
				{
					ToPin = P;
					break;
				}
			}
		}

		if (!ToPin)
		{
			OutError = FString::Printf(TEXT("SetVariable 找不到值输入 Pin。\n可用 Pins:\n%s"), *DumpNodePinsForDebug(Node));
			return false;
		}

		if (const UEdGraphSchema* Schema = FromPin->GetSchema())
		{
			Schema->TryCreateConnection(FromPin, ToPin);
		}
	}

	if (!Step.DefaultValue.IsEmpty())
	{
		TryApplyVariableSetLiteralValue(Node, Step.VarName, Step.DefaultValue);
	}

	return true;
}

/** DynamicCast 的成功类型输出（As Character 等），排除输入 Object。 */
static UEdGraphPin* FindDynamicCastTypedOutputPin(UK2Node_DynamicCast* CastNode)
{
	if (!CastNode)
	{
		return nullptr;
	}

	UEdGraphPin* Fallback = nullptr;
	for (UEdGraphPin* P : CastNode->Pins)
	{
		if (!P || P->Direction != EGPD_Output)
		{
			continue;
		}
		if (P->PinType.PinCategory != UEdGraphSchema_K2::PC_Object &&
			P->PinType.PinCategory != UEdGraphSchema_K2::PC_Interface)
		{
			continue;
		}
		const FString PinName = P->PinName.ToString();
		if (PinName.Equals(TEXT("Object"), ESearchCase::IgnoreCase))
		{
			continue;
		}
		if (PinName.StartsWith(TEXT("As"), ESearchCase::IgnoreCase))
		{
			return P;
		}
		if (!Fallback)
		{
			Fallback = P;
		}
	}
	return Fallback;
}

static bool IsVariableNodeSelfPinRequest(const FString& PinName)
{
	const FString Trimmed = PinName.TrimStartAndEnd();
	return Trimmed.Equals(TEXT("self"), ESearchCase::IgnoreCase) ||
		   Trimmed.Equals(TEXT("Target"), ESearchCase::IgnoreCase);
}

static FEdGraphPinType MakeFallbackIntPinType()
{
	FEdGraphPinType PinType;
	PinType.PinCategory = UEdGraphSchema_K2::PC_Int;
	return PinType;
}

/** 变量在目标类上尚未解析时，仍生成可连线的 self / 值 pin（跨 Actor 同名变量场景）。 */
static bool EnsureExternalVariableNodePins(UEdGraphNode* VariableNode, FName VarName, UClass* ContextClass)
{
	UK2Node_Variable* VarBase = Cast<UK2Node_Variable>(VariableNode);
	if (!VarBase || !ContextClass || VarName.IsNone())
	{
		return false;
	}

	const UEdGraphSchema_K2* K2Schema = GetDefault<UEdGraphSchema_K2>();
	VarBase->Modify();
	VarBase->VariableReference.SetExternalMember(VarName, ContextClass);

	TArray<UEdGraphPin*> OldPins = VarBase->Pins;
	VarBase->BreakAllNodeLinks();

	if (UK2Node_VariableGet* VG = Cast<UK2Node_VariableGet>(VarBase))
	{
		VG->ReallocatePinsDuringReconstruction(OldPins);
	}
	else if (UK2Node_VariableSet* VS = Cast<UK2Node_VariableSet>(VarBase))
	{
		VS->ReallocatePinsDuringReconstruction(OldPins);
	}

	if (K2Schema->FindSelfPin(*VarBase, EGPD_Input) != nullptr)
	{
		return true;
	}

	// 属性尚不存在（如 Character 上的 CoinCount 只在 BP_Player 子类里）：手动补 pin，编译期再报错即可。
	VarBase->Pins.Empty();
	if (UK2Node_VariableSet* VS = Cast<UK2Node_VariableSet>(VarBase))
	{
		VS->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Exec, UEdGraphSchema_K2::PN_Execute);
		VS->CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, UEdGraphSchema_K2::PN_Then);
	}

	VarBase->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Object, ContextClass, UEdGraphSchema_K2::PN_Self);

	const FEdGraphPinType ValueType = MakeFallbackIntPinType();
	if (UK2Node_VariableGet* VG = Cast<UK2Node_VariableGet>(VarBase))
	{
		if (UEdGraphPin* ValuePin = VG->CreatePin(EGPD_Output, ValueType, VarName))
		{
			K2Schema->SetPinAutogeneratedDefaultValueBasedOnType(ValuePin);
		}
	}
	else if (UK2Node_VariableSet* VS = Cast<UK2Node_VariableSet>(VarBase))
	{
		if (UEdGraphPin* InVal = VS->CreatePin(EGPD_Input, ValueType, VarName))
		{
			K2Schema->SetPinAutogeneratedDefaultValueBasedOnType(InVal);
		}
		if (UEdGraphPin* OutVal = VS->CreatePin(EGPD_Output, ValueType, VarName))
		{
			K2Schema->SetPinAutogeneratedDefaultValueBasedOnType(OutVal);
		}
	}

	return K2Schema->FindSelfPin(*VarBase, EGPD_Input) != nullptr;
}

static bool TryRetargetVariableNodeSelfContext(UEdGraphNode* VariableNode, UEdGraphPin* SourceObjectPin)
{
	if (!VariableNode)
	{
		return false;
	}

	UClass* SourceClass = nullptr;
	if (SourceObjectPin)
	{
		if (SourceObjectPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object ||
			SourceObjectPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Interface ||
			(SourceObjectPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard &&
			 Cast<UK2Node_DynamicCast>(SourceObjectPin->GetOwningNode()) != nullptr))
		{
			SourceClass = Cast<UClass>(SourceObjectPin->PinType.PinSubCategoryObject.Get());
			if (!SourceClass)
			{
				if (UK2Node_DynamicCast* CastNode = Cast<UK2Node_DynamicCast>(SourceObjectPin->GetOwningNode()))
				{
					SourceClass = CastNode->TargetType;
				}
			}
		}
	}

	if (!SourceClass)
	{
		return false;
	}

	auto ApplyExternalMember = [&](auto* VarNode, FName MemberName) -> bool
	{
		if (MemberName.IsNone())
		{
			return false;
		}
		// 跨 Actor 变量读写（如金币拾取 CoinCount）：允许先重定向到 Cast 目标类，即使属性尚未在 C++ 基类上声明。
		return EnsureExternalVariableNodePins(VarNode, MemberName, SourceClass);
	};

	// 仅处理 VariableGet / VariableSet
	if (UK2Node_VariableGet* VG = Cast<UK2Node_VariableGet>(VariableNode))
	{
		return ApplyExternalMember(VG, VG->VariableReference.GetMemberName());
	}
	if (UK2Node_VariableSet* VS = Cast<UK2Node_VariableSet>(VariableNode))
	{
		return ApplyExternalMember(VS, VS->VariableReference.GetMemberName());
	}

	return false;
}

/** 金币拾取等：Cast → Get/Set self。LLM 常把 Cast 输入 Object 误当输出；本地 BP 无该变量时 pin 尚未生成。 */
static bool TryPrepareCrossActorVariableSelfConnection(
	UEdGraphNode* FromNode,
	UEdGraphPin*& InOutFromPin,
	UEdGraphNode* ToNode,
	const FString& RequestedFromPin,
	const FString& RequestedToPin,
	UEdGraphPin*& OutToPin)
{
	OutToPin = nullptr;
	if (!FromNode || !ToNode)
	{
		return false;
	}

	const bool bToIsVariableNode =
		Cast<UK2Node_VariableGet>(ToNode) != nullptr ||
		Cast<UK2Node_VariableSet>(ToNode) != nullptr;
	if (!bToIsVariableNode || !IsVariableNodeSelfPinRequest(RequestedToPin))
	{
		return false;
	}

	UEdGraphPin* SourceForRetarget = InOutFromPin;
	if (UK2Node_DynamicCast* CastNode = Cast<UK2Node_DynamicCast>(FromNode))
	{
		const bool bMiswiredCastObject =
			RequestedFromPin.Equals(TEXT("Object"), ESearchCase::IgnoreCase) ||
			(InOutFromPin &&
			 InOutFromPin->Direction == EGPD_Input &&
			 InOutFromPin->PinName.ToString().Equals(TEXT("Object"), ESearchCase::IgnoreCase));
		if (bMiswiredCastObject)
		{
			if (UEdGraphPin* AsOut = FindDynamicCastTypedOutputPin(CastNode))
			{
				InOutFromPin = AsOut;
				SourceForRetarget = AsOut;
			}
		}
		else if (!SourceForRetarget)
		{
			SourceForRetarget = FindDynamicCastTypedOutputPin(CastNode);
			if (SourceForRetarget)
			{
				InOutFromPin = SourceForRetarget;
			}
		}
	}

	if (!TryRetargetVariableNodeSelfContext(ToNode, SourceForRetarget))
	{
		if (UK2Node_DynamicCast* CastNode = Cast<UK2Node_DynamicCast>(FromNode))
		{
			if (UEdGraphPin* AsOut = FindDynamicCastTypedOutputPin(CastNode))
			{
				if (!TryRetargetVariableNodeSelfContext(ToNode, AsOut))
				{
					return false;
				}
				InOutFromPin = AsOut;
			}
			else
			{
				return false;
			}
		}
		else
		{
			return false;
		}
	}

	OutToPin = FindPinLoose(ToNode, RequestedToPin.IsEmpty() ? TEXT("self") : RequestedToPin);
	return OutToPin != nullptr;
}

static bool ExecuteConnectPins(const FBlueprintDslActionStep& Step, UBlueprint* Blueprint, UEdGraph* Graph, FString& OutError)
{
	if (!Graph)
	{
		OutError = TEXT("目标图为空。");
		return false;
	}

	auto PinTypeToCompactString = [](const FEdGraphPinType& T) -> FString
	{
		FString S = T.PinCategory.ToString();
		if (!T.PinSubCategory.IsNone())
		{
			S += FString::Printf(TEXT("<%s>"), *T.PinSubCategory.ToString());
		}
		if (T.PinSubCategoryObject.IsValid())
		{
			S += FString::Printf(TEXT("(%s)"), *T.PinSubCategoryObject->GetName());
		}
		switch (T.ContainerType)
		{
		case EPinContainerType::Array: S = FString::Printf(TEXT("TArray<%s>"), *S); break;
		case EPinContainerType::Set:   S = FString::Printf(TEXT("TSet<%s>"), *S); break;
		case EPinContainerType::Map:
		{
			FString V = T.PinValueType.TerminalCategory.ToString();
			if (!T.PinValueType.TerminalSubCategory.IsNone())
			{
				V += FString::Printf(TEXT("<%s>"), *T.PinValueType.TerminalSubCategory.ToString());
			}
			if (T.PinValueType.TerminalSubCategoryObject.IsValid())
			{
				V += FString::Printf(TEXT("(%s)"), *T.PinValueType.TerminalSubCategoryObject->GetName());
			}
			S = FString::Printf(TEXT("TMap<%s,%s>"), *S, *V);
			break;
		}
		default:
			break;
		}
		return S;
	};

	UEdGraphNode* FromNode = FindNodeByNodeIdOrSpecial(Blueprint, Graph, Step.FromNodeId);
	UEdGraphNode* ToNode = FindNodeByNodeIdOrSpecial(Blueprint, Graph, Step.ToNodeId);
	if (!FromNode)
	{
		// 如果是 BeginPlay 引用但图里没有，自动创建一个（更贴合通用 DSL）
		if (IsBeginPlayNodeId(Step.FromNodeId))
		{
			UK2Node_Event* BeginPlayNode = nullptr;
			if (EnsureBeginPlayEvent(Graph, 120, 120, BeginPlayNode, OutError) && BeginPlayNode)
			{
				FromNode = BeginPlayNode;
				TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
			}
		}
		// 如果是 ActorBeginOverlap 引用但图里没有，自动创建一个（金币拾取等场景常用）
		if (!FromNode && IsActorBeginOverlapNodeId(Step.FromNodeId))
		{
			UK2Node_Event* OverlapNode = nullptr;
			if (EnsureActorBeginOverlapEvent(Graph, 120, 180, OverlapNode, OutError) && OverlapNode)
			{
				FromNode = OverlapNode;
				TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
			}
		}
		// AnyDamage：角色受伤逻辑常用；若图里没有则自动创建
		if (!FromNode && IsAnyDamageNodeId(Step.FromNodeId))
		{
			UK2Node_Event* DamageNode = nullptr;
			if (EnsureAnyDamageEvent(Graph, 120, 150, DamageNode, OutError) && DamageNode)
			{
				FromNode = DamageNode;
				TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
			}
		}
		// ActorEndOverlap：触发器“离开”常用；若图里没有则自动创建
		if (!FromNode && IsActorEndOverlapNodeId(Step.FromNodeId))
		{
			UK2Node_Event* OverlapNode = nullptr;
			if (EnsureActorEndOverlapEvent(Graph, 120, 210, OverlapNode, OutError) && OverlapNode)
			{
				FromNode = OverlapNode;
				TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
			}
		}
		// ComponentBeginOverlap：优先组件绑定事件（更贴近拾取物/触发器）
		if (!FromNode && IsComponentBeginOverlapNodeId(Step.FromNodeId))
		{
			UK2Node_Event* OverlapNode = nullptr;
			if (EnsureComponentBeginOverlapEvent(Blueprint, Graph, 120, 180, OverlapNode, OutError) && OverlapNode)
			{
				FromNode = OverlapNode;
				TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
			}
			else
			{
				// P0 回退策略：若组件事件不可绑定/不可创建，则自动退化到 ActorBeginOverlap。
				// 这样即便当前蓝图没有可绑定的 PrimitiveComponent，也能继续执行主链。
				FString FallbackErr;
				UK2Node_Event* ActorOverlapNode = nullptr;
				if (EnsureActorBeginOverlapEvent(Graph, 120, 180, ActorOverlapNode, FallbackErr) && ActorOverlapNode)
				{
					FromNode = ActorOverlapNode;
					// 仍用 OnComponentBeginOverlap 语义 id 打 tag，后续 ConnectPins 可复用同一节点。
					TagAutoCreatedEventNode(FromNode, Step.FromNodeId);
				}
			}
		}
		if (!FromNode && IsInputKeyNodeId(Step.FromNodeId))
		{
			FKey InputKey;
			if (TryResolveInputKeyFromDslToken(Step.FromNodeId, InputKey))
			{
				UK2Node_InputKey* InputNode = nullptr;
				if (EnsureInputKeyEvent(Graph, InputKey, 120, 240, InputNode, OutError) && InputNode)
				{
					FromNode = InputNode;
				}
			}
		}
		if (!FromNode)
		{
			OutError = FString::Printf(TEXT("未找到 fromNodeId=%s 对应节点。"), *Step.FromNodeId);
			return false;
		}
	}
	if (!ToNode)
	{
		OutError = FString::Printf(TEXT("未找到 toNodeId=%s 对应节点。"), *Step.ToNodeId);
		return false;
	}

	UEdGraphPin* FromPin = FindPinLoose(FromNode, Step.FromPin);
	UEdGraphPin* ToPin = FindPinLoose(ToNode, Step.ToPin);

	// 跨 Actor 变量 self：Cast → Get/Set。本地 BP 无同名变量时节点可能尚无 self pin；Cast.Object 也常误连输入 pin。
	{
		UEdGraphPin* PreparedToPin = nullptr;
		if (TryPrepareCrossActorVariableSelfConnection(FromNode, FromPin, ToNode, Step.FromPin, Step.ToPin, PreparedToPin))
		{
			ToPin = PreparedToPin;
		}
	}

	// 方向感知的 Exec 兜底：只在 LLM 写了 "Exec"/"Then"/"Out" 之类别名时生效，
	// 避免错误地把其它数据 pin 当成 exec pin。
	if (!FromPin && (IsInputExecAlias(Step.FromPin) || IsOutputExecAlias(Step.FromPin)))
	{
		FromPin = FindFirstExecPinByDirection(FromNode, EGPD_Output);
	}
	if (!ToPin && (IsInputExecAlias(Step.ToPin) || IsOutputExecAlias(Step.ToPin)))
	{
		ToPin = FindFirstExecPinByDirection(ToNode, EGPD_Input);
	}

	if (!FromPin)
	{
		OutError = FString::Printf(TEXT("未找到 fromPin=%s。\n可用 Pins:\n%s"), *Step.FromPin, *DumpNodePinsForDebug(FromNode));
		return false;
	}
	if (!ToPin)
	{
		FString Msg = FString::Printf(TEXT("未找到 toPin=%s。\n可用 Pins:\n%s"), *Step.ToPin, *DumpNodePinsForDebug(ToNode));
		// Pure 函数（如 GetPlayerCharacter）没有 execute 输入；执行线应接到 Cast/Branch/PrintString 等有 Exec 的节点。
		if (IsInputExecAlias(Step.ToPin) && IsK2NodePureNoExec(ToNode))
		{
			Msg += TEXT(
				"\n提示：目标节点为 Pure（无白色执行引脚），不能把 BeginPlay 的 then 连到该节点的 execute。"
				"正确做法：ConnectPins 把 Event BeginPlay 的 then → Cast 的 execute；再用数据线把 GetPlayerCharacter 的 ReturnValue → Cast 的 Object。");
		}
		OutError = Msg;
		return false;
	}

	const UEdGraphSchema* Schema = FromPin->GetSchema();
	if (!Schema)
	{
		OutError = TEXT("无法获取 EdGraph Schema。");
		return false;
	}

	// Phase 5：连线静态预检（先告诉用户「为什么不能连」和两端类型；减少黑盒 TryCreateConnection 失败）
	{
		const auto Resp = Schema->CanCreateConnection(FromPin, ToPin);
		const bool bCanConnect =
			(Resp.Response == ECanCreateConnectionResponse::CONNECT_RESPONSE_MAKE) ||
			(Resp.Response == ECanCreateConnectionResponse::CONNECT_RESPONSE_BREAK_OTHERS_A) ||
			(Resp.Response == ECanCreateConnectionResponse::CONNECT_RESPONSE_BREAK_OTHERS_B) ||
			(Resp.Response == ECanCreateConnectionResponse::CONNECT_RESPONSE_BREAK_OTHERS_AB) ||
			(Resp.Response == ECanCreateConnectionResponse::CONNECT_RESPONSE_MAKE_WITH_CONVERSION_NODE);

		if (!bCanConnect)
		{
			// 允许后续兜底（例如 float→string 自动插转换），不要在这里直接 return。
			const FString FromType = PinTypeToCompactString(FromPin->PinType);
			const FString ToType = PinTypeToCompactString(ToPin->PinType);
			const FString Reason = Resp.Message.ToString();
			OutError = FString::Printf(
				TEXT("连线预检失败：%s.%s (%s) -> %s.%s (%s)\n原因：%s"),
				*Step.FromNodeId, *Step.FromPin, *FromType,
				*Step.ToNodeId, *Step.ToPin, *ToType,
				Reason.IsEmpty() ? TEXT("(无)") : *Reason);
		}
	}

	if (TryConnectHitResultToCastObject(Graph, FromPin, ToNode, ToPin))
	{
		return true;
	}

	if (Schema->TryCreateConnection(FromPin, ToPin))
	{
		return true;
	}

	// P1：跨对象变量读写支持（最小可用版）
	// 当目标是 GetVariable/SetVariable 的 self 引脚，且来源是对象输出时：
	// 1) 根据来源对象类把 Variable 节点从 SelfMember 重定向到 ExternalMember；
	// 2) 重新连接。
	{
		const bool bToIsSelfObjectPin =
			ToPin &&
			ToPin->Direction == EGPD_Input &&
			ToPin->PinName.ToString().Equals(TEXT("self"), ESearchCase::IgnoreCase) &&
			(ToPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object ||
			 ToPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Interface);
		const bool bFromIsObjectLike =
			FromPin &&
			FromPin->Direction == EGPD_Output &&
			(FromPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object ||
			 FromPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Interface ||
			 (FromPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard &&
			  Cast<UK2Node_DynamicCast>(FromNode) != nullptr));
		const bool bToIsVariableNode =
			Cast<UK2Node_VariableGet>(ToNode) != nullptr ||
			Cast<UK2Node_VariableSet>(ToNode) != nullptr;
		const bool bFromCastObjectInputToVarSelf =
			bToIsSelfObjectPin &&
			bToIsVariableNode &&
			Cast<UK2Node_DynamicCast>(FromNode) != nullptr &&
			FromPin &&
			FromPin->Direction == EGPD_Input &&
			FromPin->PinName.ToString().Equals(TEXT("Object"), ESearchCase::IgnoreCase);

		if ((bToIsSelfObjectPin && bFromIsObjectLike && bToIsVariableNode) || bFromCastObjectInputToVarSelf)
		{
			if (TryRetargetVariableNodeSelfContext(ToNode, FromPin))
			{
				if (UEdGraphPin* NewToPin = FindPinLoose(ToNode, Step.ToPin.IsEmpty() ? TEXT("self") : Step.ToPin))
				{
					if (const UEdGraphSchema* S2 = FromPin->GetSchema())
					{
						if (S2->TryCreateConnection(FromPin, NewToPin))
						{
							return true;
						}
					}
				}
			}
		}
	}

	// 兜底0：DynamicCast 常见误连 —— 把 Cast 的输入 Object 当成“输出对象”去接下游 self。
	// 若 from 是 DynamicCast 且 fromPin 指向输入 Object，但 to 需要 object/self，则自动改用 Cast 的输出对象（AsXXX）再尝试。
	{
		UK2Node_DynamicCast* CastNode = Cast<UK2Node_DynamicCast>(FromNode);
		const bool bFromIsCastObjectInput =
			(CastNode != nullptr) &&
			(FromPin->Direction == EGPD_Input) &&
			FromPin->PinName.ToString().Equals(TEXT("Object"), ESearchCase::IgnoreCase);
		const bool bToWantsObjectLike =
			(ToPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object) ||
			(ToPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Interface);

		if (bFromIsCastObjectInput && bToWantsObjectLike)
		{
			UEdGraphPin* AsOut = nullptr;
			for (UEdGraphPin* P : FromNode->Pins)
			{
				if (!P || P->Direction != EGPD_Output)
				{
					continue;
				}
				if (P->PinType.PinCategory != UEdGraphSchema_K2::PC_Object && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Interface)
				{
					continue;
				}
				const FString N = P->PinName.ToString();
				const FString D = P->GetDisplayName().ToString();
				if (N.StartsWith(TEXT("As"), ESearchCase::IgnoreCase) || D.StartsWith(TEXT("As"), ESearchCase::IgnoreCase))
				{
					AsOut = P;
					break;
				}
				// 某些本地化环境下 display 不是 As 开头，但输出对象通常只有一个 object 输出，先记下兜底
				if (!AsOut)
				{
					AsOut = P;
				}
			}

			if (AsOut)
			{
				// 如果目标是变量 self 引脚，先尝试把变量节点重定向到 AsOut 的对象类。
				TryRetargetVariableNodeSelfContext(ToNode, AsOut);
				if (UEdGraphPin* NewToPin = FindPinLoose(ToNode, Step.ToPin.IsEmpty() ? TEXT("self") : Step.ToPin))
				{
					ToPin = NewToPin;
				}
				if (const UEdGraphSchema* S2 = AsOut->GetSchema())
				{
					if (S2->TryCreateConnection(AsOut, ToPin))
					{
						return true;
					}
				}
			}
		}
	}

	// 兜底1：LineTrace.OutHit(FHitResult) -> Cast.Object 常被模型误连（TryConnectHitResultToCastObject 已在直连前尝试过，此处再兜底一次）。
	if (TryConnectHitResultToCastObject(Graph, FromPin, ToNode, ToPin))
	{
		return true;
	}

	// 兜底2：常见误连 —— 把数据输出（例如 Cast.Object）连到 execute 输入。
	// 若目标是 exec 输入，且来源节点有 exec 输出，则自动改连来源首个 exec 输出。
	{
		const bool bToIsExecInput =
			ToPin->Direction == EGPD_Input &&
			ToPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec;
		const bool bFromIsNonExec =
			FromPin->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec;
		if (bToIsExecInput && bFromIsNonExec)
		{
			if (UEdGraphPin* FromExec = FindFirstExecPinByDirection(FromNode, EGPD_Output))
			{
				if (FromExec != FromPin)
				{
					if (const UEdGraphSchema* S = FromExec->GetSchema())
					{
						if (S->TryCreateConnection(FromExec, ToPin))
						{
							return true;
						}
					}
				}
			}
		}
	}

	// 兜底：int/float/bool/name → string（常见于 PrintString.InString）。自动插入 Conv_*ToString。
	if (TryAutoInsertConvToString(Graph, FromPin, ToNode, ToPin))
	{
		return true;
	}

	// 若预检已写入更精确的原因与类型信息，则保留；否则给出兜底错误。
	if (OutError.IsEmpty())
	{
		const FString FromType = PinTypeToCompactString(FromPin->PinType);
		const FString ToType = PinTypeToCompactString(ToPin->PinType);
		OutError = FString::Printf(
			TEXT("连接失败：%s.%s (%s) -> %s.%s (%s)"),
			*Step.FromNodeId, *Step.FromPin, *FromType,
			*Step.ToNodeId, *Step.ToPin, *ToType);
	}
	return false;
}

static bool ExecuteSetPinDefault(const FBlueprintDslActionStep& Step, UEdGraph* Graph, FString& OutError)
{
	if (!Graph)
	{
		OutError = TEXT("目标图为空。");
		return false;
	}

	UEdGraphNode* Node = FindNodeByNodeIdOrSpecial(Graph, Step.NodeId);
	if (!Node)
	{
		OutError = FString::Printf(TEXT("未找到 nodeId=%s 对应节点。"), *Step.NodeId);
		return false;
	}

	// Select：先定型再 SetDefault，否则 Wildcard 上写字面量在编辑器里不显示（常见于「先 SetPinDefault 后 ConnectPins」的 DSL 顺序）
	if (UK2Node_Select* Sel = Cast<UK2Node_Select>(Node))
	{
		EnsureSelectPinsTypedForLiteralDefaults(Sel);
	}

	UEdGraphPin* Pin = FindPinLoose(Node, Step.PinName);
	if (!Pin)
	{
		OutError = FString::Printf(TEXT("未找到 pinName=%s。\n可用 Pins:\n%s"), *Step.PinName, *DumpNodePinsForDebug(Node));
		return false;
	}

	if (Pin->Direction != EGPD_Input)
	{
		OutError = FString::Printf(TEXT("引脚 %s 不是输入引脚，无法设置默认值。"), *Step.PinName);
		return false;
	}
	if (Pin->LinkedTo.Num() > 0)
	{
		OutError = FString::Printf(TEXT("引脚 %s 已连接，无法设置默认值。"), *Step.PinName);
		return false;
	}

	Pin->Modify();

	// 支持对象/类默认值（用于 AnimMontage、SpawnActor Class 等）
	if (Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object || Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Class)
	{
		const FString PathOrName = Step.DefaultValue.TrimStartAndEnd();
		if (PathOrName.IsEmpty())
		{
			Pin->DefaultObject = nullptr;
			Pin->DefaultValue.Empty();
			return true;
		}

		// 1) 按短名 / 完整路径解析 Class（SpawnActor 的 CreateNode 阶段已做白名单检查，这里放宽）
		if (Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Class)
		{
			if (UClass* C = ResolveClassByName(PathOrName))
			{
				Pin->DefaultObject = C;
				Pin->DefaultValue.Empty();
				return true;
			}
		}

		// 2) 尝试用字符串查找已加载对象
		if (UObject* Obj = FindFirstObject<UObject>(*PathOrName, EFindFirstObjectOptions::None))
		{
			Pin->DefaultObject = Obj;
			Pin->DefaultValue.Empty();
			return true;
		}

		// 3) 尝试按 /Game 路径加载（仅在用户给出完整路径时可用）
		if (PathOrName.StartsWith(TEXT("/Game/"), ESearchCase::IgnoreCase))
		{
			// 不强制类型；Pin 类型校验留给 TryCreateConnection/运行时
			UObject* Loaded = LoadObject<UObject>(nullptr, *PathOrName);
			if (Loaded)
			{
				Pin->DefaultObject = Loaded;
				Pin->DefaultValue.Empty();
				return true;
			}
		}
	}

	// 普通标量默认值
	if (const UEdGraphSchema* Schema = Pin->GetSchema())
	{
		// 走 Schema 的设置接口，确保 UI/编译器侧一致（MakeArray 元素 pin 等场景更稳）
		Schema->TrySetDefaultValue(*Pin, Step.DefaultValue);
		if (UEdGraphNode* Owning = Pin->GetOwningNode())
		{
			Owning->PinDefaultValueChanged(Pin);
		}
	}
	else
	{
		Pin->DefaultValue = Step.DefaultValue;
		if (UEdGraphNode* Owning = Pin->GetOwningNode())
		{
			Owning->PinDefaultValueChanged(Pin);
		}
	}
	return true;
}

static bool ExecuteComment(const FBlueprintDslActionStep& Step, UEdGraph* Graph, FString& OutError)
{
	if (!Graph)
	{
		OutError = TEXT("目标图为空。");
		return false;
	}

	UEdGraphNode_Comment* CommentNode = NewObject<UEdGraphNode_Comment>(Graph);
	CommentNode->NodePosX = 200;
	CommentNode->NodePosY = 200;
	CommentNode->NodeWidth = 700;
	CommentNode->NodeHeight = 200;
	Graph->AddNode(CommentNode, true, false);
	CommentNode->CreateNewGuid();
	CommentNode->SnapToGrid(16);
	CommentNode->PostPlacedNewNode();
	// 某些版本里 PostPlacedNewNode 可能重置注释文本，放在最后写入更稳妥。
	CommentNode->Modify();
	CommentNode->NodeComment = Step.CommentText.Left(2000);
	Graph->NotifyNodeChanged(CommentNode);
	return true;
}

bool FBlueprintDslExecutor::ExecuteStep(const FBlueprintDslActionStep& Step, UBlueprint* Blueprint, FString& OutError,
	FDslStepFailure* OutFailure)
{
	OutError.Empty();

	// 一些 action 是“改 Blueprint 资产本体”，不依赖 targetGraph（且可能需要先加载 targetBlueprint）
	UBlueprint* EffectiveBP = Blueprint;
	if (Step.ActionType == EBlueprintDslActionType::CreateMemberVariable ||
		Step.ActionType == EBlueprintDslActionType::CreateFunctionGraph)
	{
		FString BpErr;
		EffectiveBP = ResolveBlueprintForStep(Blueprint, Step, BpErr);
		if (!EffectiveBP)
		{
			OutError = BpErr;
			if (OutFailure)
			{
				OutFailure->Category = EDslFailureCategory::GraphResolve;
				OutFailure->RawError = OutError;
				OutFailure->StepId   = Step.StepId;
			}
			return false;
		}
	}

	FString GraphError;
	UEdGraph* Graph = nullptr;
	if (Step.ActionType != EBlueprintDslActionType::CreateMemberVariable &&
		Step.ActionType != EBlueprintDslActionType::CreateFunctionGraph)
	{
		Graph = ResolveTargetGraphForStep(EffectiveBP, Step, GraphError);
		if (!Graph)
		{
			OutError = GraphError;
			if (OutFailure)
			{
				OutFailure->Category  = EDslFailureCategory::GraphResolve;
				OutFailure->RawError  = OutError;
				OutFailure->StepId    = Step.StepId;
			}
			return false;
		}
	}

	bool bOk = false;
	switch (Step.ActionType)
	{
	case EBlueprintDslActionType::CreateNode:
		bOk = ExecuteCreateNode(Step, EffectiveBP, Graph, OutError);
		break;
	case EBlueprintDslActionType::GetVariable:
		bOk = ExecuteGetVariableAction(Step, Graph, OutError);
		break;
	case EBlueprintDslActionType::SetVariable:
		bOk = ExecuteSetVariableAction(Step, Graph, OutError);
		break;
	case EBlueprintDslActionType::ConnectPins:
		bOk = ExecuteConnectPins(Step, Blueprint, Graph, OutError);
		break;
	case EBlueprintDslActionType::SetPinDefault:
		bOk = ExecuteSetPinDefault(Step, Graph, OutError);
		break;
	case EBlueprintDslActionType::Comment:
		bOk = ExecuteComment(Step, Graph, OutError);
		break;
	case EBlueprintDslActionType::CreateMemberVariable:
		bOk = ExecuteCreateMemberVariable(Step, EffectiveBP, OutError);
		break;
	case EBlueprintDslActionType::CreateFunctionGraph:
		bOk = ExecuteCreateFunctionGraph(Step, EffectiveBP, OutError);
		break;
	default:
		OutError = TEXT("未知 action，无法执行。");
		if (OutFailure)
		{
			OutFailure->Category = EDslFailureCategory::Unknown;
			OutFailure->RawError = OutError;
			OutFailure->StepId   = Step.StepId;
		}
		return false;
	}

	if (!bOk && OutFailure)
	{
		// 根据 ActionType + 错误文本推断精确 category
		if (Step.ActionType == EBlueprintDslActionType::ConnectPins)
		{
			if (OutError.Contains(TEXT("未找到 fromNodeId=")) || OutError.Contains(TEXT("未找到 toNodeId=")))
			{
				OutFailure->Category = EDslFailureCategory::NodeResolve;
				OutFailure->NodeId   = OutError.Contains(TEXT("fromNodeId=")) ? Step.FromNodeId : Step.ToNodeId;
			}
			else if (OutError.Contains(TEXT("未找到 fromPin=")) || OutError.Contains(TEXT("未找到 toPin=")))
			{
				OutFailure->Category = EDslFailureCategory::PinResolve;
				OutFailure->PinName  = OutError.Contains(TEXT("fromPin=")) ? Step.FromPin : Step.ToPin;
			}
			else if (OutError.Contains(TEXT("连线预检失败")))
			{
				OutFailure->Category  = EDslFailureCategory::ConnectPrecheck;
				OutFailure->PinName   = Step.FromPin;
				OutFailure->FromPin   = Step.FromPin;
				OutFailure->ToPin     = Step.ToPin;
			}
			else
			{
				OutFailure->Category = EDslFailureCategory::ConnectFail;
			}
			OutFailure->FromNodeId = Step.FromNodeId;
			OutFailure->FromPin    = Step.FromPin;
			OutFailure->ToNodeId   = Step.ToNodeId;
			OutFailure->ToPin      = Step.ToPin;
		}
		else if (Step.ActionType == EBlueprintDslActionType::CreateNode ||
			     Step.ActionType == EBlueprintDslActionType::GetVariable ||
			     Step.ActionType == EBlueprintDslActionType::SetVariable)
		{
			if (OutError.Contains(TEXT("functionName")) || OutError.Contains(TEXT("未找到函数")) ||
				OutError.Contains(TEXT("不受支持")))
			{
				OutFailure->Category = EDslFailureCategory::FunctionNotFound;
			}
			else if (OutError.Contains(TEXT("targetClass")) || OutError.Contains(TEXT("UClass")))
			{
				OutFailure->Category = EDslFailureCategory::ClassNotFound;
			}
			else
			{
				OutFailure->Category = EDslFailureCategory::Unknown;
			}
			OutFailure->NodeId = Step.NodeId;
		}
		else if (Step.ActionType == EBlueprintDslActionType::SetPinDefault)
		{
			if (OutError.Contains(TEXT("未找到 nodeId=")))
			{
				OutFailure->Category = EDslFailureCategory::NodeResolve;
				OutFailure->NodeId   = Step.NodeId;
			}
			else if (OutError.Contains(TEXT("未找到 pinName=")))
			{
				OutFailure->Category = EDslFailureCategory::PinResolve;
				OutFailure->PinName  = Step.PinName;
			}
			else
			{
				OutFailure->Category = EDslFailureCategory::SetPinFail;
			}
		}
		else
		{
			OutFailure->Category = FDslStepFailure::CategorizeFromText(OutError);
		}

		OutFailure->StepId   = Step.StepId;
		OutFailure->RawError = OutError;
	}

	if (bOk && EffectiveBP)
	{
		FBlueprintEditorUtils::MarkBlueprintAsStructurallyModified(EffectiveBP);
	}
	return bOk;
}

void FBlueprintDslExecutor::ValidateSteps(const TArray<FBlueprintDslActionStep>& Steps, UBlueprint* Blueprint, TArray<FValidationIssue>& OutIssues)
{
	OutIssues.Reset();

	if (!Blueprint)
	{
		FValidationIssue I;
		I.Severity = FValidationIssue::ESeverity::Error;
		I.StepIndex = INDEX_NONE;
		I.Message = TEXT("Blueprint 为空。");
		OutIssues.Add(I);
		return;
	}

	// 收集变量白名单（当前 BP 成员变量）
	TSet<FName> AllowedVars;
	for (const FBPVariableDescription& VarDesc : Blueprint->NewVariables)
	{
		AllowedVars.Add(VarDesc.VarName);
	}

	// 预扫描：将 DSL 中声明要创建的成员变量也加入白名单（并允许后续步骤使用）
	TSet<FName> WillCreateVars;
	for (const FBlueprintDslActionStep& Step : Steps)
	{
		if (Step.ActionType == EBlueprintDslActionType::CreateMemberVariable && !Step.VarName.TrimStartAndEnd().IsEmpty())
		{
			const FName N(*Step.VarName.TrimStartAndEnd());
			AllowedVars.Add(N);
			WillCreateVars.Add(N);
		}
	}

	TSet<FString> DeclaredNodeIds;
	for (int32 i = 0; i < Steps.Num(); ++i)
	{
		const FBlueprintDslActionStep& Step = Steps[i];

		// 风险标记
		if (Step.bRequiresConfirmation)
		{
			FValidationIssue W;
			W.Severity = FValidationIssue::ESeverity::Warning;
			W.StepIndex = i;
			W.Message = TEXT("该步骤标记为 requiresConfirmation=true（高风险/需确认）。建议执行前取消勾选或分步执行。");
			OutIssues.Add(W);
		}

		// nodeId 唯一性：只对“声明节点”的步骤生效（避免 SetPinDefault 这类引用步骤误判重复）
		const bool bDeclaresNodeId =
			Step.ActionType == EBlueprintDslActionType::CreateNode ||
			Step.ActionType == EBlueprintDslActionType::GetVariable ||
			Step.ActionType == EBlueprintDslActionType::SetVariable;

		if (bDeclaresNodeId)
		{
			if (Step.NodeId.IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("声明节点步骤缺少 nodeId。");
				OutIssues.Add(E);
			}
			else if (DeclaredNodeIds.Contains(Step.NodeId))
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = FString::Printf(TEXT("nodeId 重复：%s"), *Step.NodeId);
				OutIssues.Add(E);
			}
			else
			{
				DeclaredNodeIds.Add(Step.NodeId);
			}
		}

		// targetGraph 基本格式（只做轻校验；真正解析在执行时）
		if (!Step.TargetGraph.IsEmpty())
		{
			const FString T = Step.TargetGraph.TrimStartAndEnd();
			if (!(T.Equals(TEXT("EventGraph"), ESearchCase::IgnoreCase) || T.StartsWith(TEXT("Function:"), ESearchCase::IgnoreCase)))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(TEXT("targetGraph=%s 可能不受支持（建议 EventGraph 或 Function:<Name>）。"), *T);
				OutIssues.Add(W);
			}
		}

		// 变量白名单
		if (Step.ActionType == EBlueprintDslActionType::GetVariable || Step.ActionType == EBlueprintDslActionType::SetVariable)
		{
			if (Step.VarName.IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("变量步骤缺少 varName。");
				OutIssues.Add(E);
			}
			else if (!AllowedVars.Contains(FName(*Step.VarName)))
			{
				// 只给 Warning：让用户看到“可补 CreateMemberVariable”，而不是到执行阶段才失败
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(
					TEXT("检测到变量可能不存在：%s。建议在首次使用前插入 CreateMemberVariable(varName=%s,varType=...) 自动创建，或由地编提前创建/确认变量名。"),
					*Step.VarName, *Step.VarName);
				OutIssues.Add(W);
			}
		}

		// CreateFunctionGraph 的基本校验
		if (Step.ActionType == EBlueprintDslActionType::CreateFunctionGraph)
		{
			if (Step.FunctionGraphName.TrimStartAndEnd().IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("CreateFunctionGraph 缺少 name。");
				OutIssues.Add(E);
			}
			if (Step.FunctionGraphKind.TrimStartAndEnd().IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("CreateFunctionGraph 缺少 kind（Callable/Pure/Event/Macro）。");
				OutIssues.Add(E);
			}
		}

		// CallFunction：函数存在性 + 高风险检查（方案 3）
		if (Step.ActionType == EBlueprintDslActionType::CreateNode &&
			Step.NodeType.Equals(TEXT("CallFunction"), ESearchCase::IgnoreCase))
		{
			const FString Func = Step.FunctionName.TrimStartAndEnd();
			if (Func.IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("CallFunction 缺少 functionName。");
				OutIssues.Add(E);
			}
			else if (!Func.Equals(TEXT("BeginPlay"), ESearchCase::IgnoreCase) &&
					 !Func.Equals(TEXT("ReceiveBeginPlay"), ESearchCase::IgnoreCase) &&
					 !IsAnyDamageNodeId(Func) &&
					 !LooksLikeMiswiredInputKeyCall(Func) &&
					 !LooksLikeSetTimerByFunctionNameCall(Func) &&
					 !Func.Equals(TEXT("GetComponentByClass"), ESearchCase::IgnoreCase) &&
					 !Func.Equals(TEXT("Get Component by Class"), ESearchCase::IgnoreCase))
			{
				const FString Resolved = ResolveFunctionNameAlias(Func);

				// 高风险：未勾选 requiresConfirmation 给 Warning（执行阶段才会 Error 拦截）
				if (IsHighRiskFunctionName(Resolved) && !Step.bRequiresConfirmation)
				{
					FValidationIssue W;
					W.Severity = FValidationIssue::ESeverity::Warning;
					W.StepIndex = i;
					W.Message = FString::Printf(
						TEXT("函数 %s 属于高风险操作，执行将被阻止，除非在 DSL 中显式设置 requiresConfirmation=true。"),
						*Resolved);
					OutIssues.Add(W);
				}

				// 函数存在性：解析失败只给 Warning（允许尝试执行，交由执行阶段给出更具体错误）
				UClass* Owner = nullptr;
				if (!ResolveCallFunctionFlexibly(Func, Owner))
				{
					FValidationIssue W;
					W.Severity = FValidationIssue::ESeverity::Warning;
					W.StepIndex = i;
					W.Message = FString::Printf(
						TEXT("未能在常用库或已加载类中解析函数：%s（执行时仍会再次尝试；若失败会回报具体错误）。若这是蓝图自定义函数，建议先插入 CreateFunctionGraph(name=%s,kind=Callable,...) 创建骨架。"),
						*Func, *Func);
					OutIssues.Add(W);
				}
			}
		}

		if (Step.ActionType == EBlueprintDslActionType::CreateNode &&
			Step.NodeType.Equals(TEXT("InputKey"), ESearchCase::IgnoreCase))
		{
			FKey InputKey;
			if (!TryResolveInputKeyForCreateStep(Step, InputKey))
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("InputKey 缺少可解析的按键名。请在 functionName 中填写键名（例如 E/F/SpaceBar），或使用像 InputKey_E 这样的 nodeId。");
				OutIssues.Add(E);
			}
			else if (!Step.TargetGraph.IsEmpty() && !Step.TargetGraph.Equals(TEXT("EventGraph"), ESearchCase::IgnoreCase))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = TEXT("InputKey 事件通常应创建在 EventGraph；如果目标图不是 EventGraph，执行可能失败。");
				OutIssues.Add(W);
			}
		}

		if (Step.ActionType == EBlueprintDslActionType::CreateNode &&
			Step.NodeType.Equals(TEXT("GetComponentByClass"), ESearchCase::IgnoreCase))
		{
			const FString CompClassName = Step.TargetClass.TrimStartAndEnd();
			if (CompClassName.IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("GetComponentByClass 需要 targetClass（组件类名）。");
				OutIssues.Add(E);
			}
			else if (!ResolveActorComponentClassByName(CompClassName))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(
					TEXT("未能解析组件类 %s（执行时仍会再次尝试）。"),
					*CompClassName);
				OutIssues.Add(W);
			}
		}

		if (Step.ActionType == EBlueprintDslActionType::CreateNode &&
			(Step.NodeType.Equals(TEXT("SetTimerByFunctionName"), ESearchCase::IgnoreCase) ||
			 Step.NodeType.Equals(TEXT("SetTimer"), ESearchCase::IgnoreCase) ||
			 Step.NodeType.Equals(TEXT("TimerByFunctionName"), ESearchCase::IgnoreCase) ||
			 Step.NodeType.Equals(TEXT("SetTimerForNextTick"), ESearchCase::IgnoreCase)))
		{
			if (Step.FunctionName.TrimStartAndEnd().IsEmpty())
			{
				FValidationIssue E;
				E.Severity = FValidationIssue::ESeverity::Error;
				E.StepIndex = i;
				E.Message = TEXT("SetTimerByFunctionName 需要 functionName（回调函数名，如 OnTimerTick）。");
				OutIssues.Add(E);
			}
		}

		// ConnectPins 引用检查（仅检查 nodeId 是否在 steps 中声明过）
		if (Step.ActionType == EBlueprintDslActionType::ConnectPins)
		{
			const bool bFromIsSpecial = IsBeginPlayNodeId(Step.FromNodeId) || IsAnyDamageNodeId(Step.FromNodeId) || IsActorBeginOverlapNodeId(Step.FromNodeId) || IsActorEndOverlapNodeId(Step.FromNodeId) || IsComponentBeginOverlapNodeId(Step.FromNodeId) || IsInputKeyNodeId(Step.FromNodeId);
			const bool bToIsSpecial = IsBeginPlayNodeId(Step.ToNodeId) || IsAnyDamageNodeId(Step.ToNodeId) || IsActorBeginOverlapNodeId(Step.ToNodeId) || IsActorEndOverlapNodeId(Step.ToNodeId) || IsComponentBeginOverlapNodeId(Step.ToNodeId) || IsInputKeyNodeId(Step.ToNodeId);

			if (!bFromIsSpecial && !DeclaredNodeIds.Contains(Step.FromNodeId))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(TEXT("fromNodeId=%s 未在前序 steps 中声明（可能依赖图中已有节点）。"), *Step.FromNodeId);
				OutIssues.Add(W);
			}
			if (!bToIsSpecial && !DeclaredNodeIds.Contains(Step.ToNodeId))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(TEXT("toNodeId=%s 未在前序 steps 中声明（可能依赖图中已有节点）。"), *Step.ToNodeId);
				OutIssues.Add(W);
			}
		}

		// SetPinDefault 引用检查（若 nodeId 没在前序声明，提示 warning）
		if (Step.ActionType == EBlueprintDslActionType::SetPinDefault)
		{
			if (!Step.NodeId.IsEmpty() && !DeclaredNodeIds.Contains(Step.NodeId))
			{
				FValidationIssue W;
				W.Severity = FValidationIssue::ESeverity::Warning;
				W.StepIndex = i;
				W.Message = FString::Printf(TEXT("SetPinDefault.nodeId=%s 未在前序 steps 中声明（可能依赖图中已有节点）。"), *Step.NodeId);
				OutIssues.Add(W);
			}
		}
	}
}

bool FBlueprintDslExecutor::ExecuteSteps(const TArray<FBlueprintDslActionStep>& Steps, UBlueprint* Blueprint,
	FString& OutSummary, TArray<FDslStepFailure>* OutFailures)
{
	auto IsKnownOptionalCustomFunction = [](const FString& FuncName) -> bool
	{
		static const TSet<FString> Names = {
			TEXT("UpdateCoinUI"),
			TEXT("RefreshCoinUI"),
			TEXT("UpdateUI"),
			TEXT("RefreshUI"),
		};
		return Names.Contains(FuncName.TrimStartAndEnd());
	};

	auto IsStepMarkedOptional = [&IsKnownOptionalCustomFunction](const FBlueprintDslActionStep& Step) -> bool
	{
		if (Step.ActionType == EBlueprintDslActionType::CreateNode &&
			Step.NodeType.Equals(TEXT("CallFunction"), ESearchCase::IgnoreCase) &&
			IsKnownOptionalCustomFunction(Step.FunctionName))
		{
			return true;
		}

		const FString Desc = Step.Description + TEXT("\n") + Step.CommentText;
		return Desc.Contains(TEXT("可选")) ||
			Desc.Contains(TEXT("optional"), ESearchCase::IgnoreCase) ||
			Desc.Contains(TEXT("若函数不存在")) ||
			Desc.Contains(TEXT("如果函数不存在")) ||
			Desc.Contains(TEXT("若不存在")) ||
			Desc.Contains(TEXT("不存在可")) ||
			Desc.Contains(TEXT("可移除")) ||
			Desc.Contains(TEXT("可删除")) ||
			Desc.Contains(TEXT("skip"), ESearchCase::IgnoreCase);
	};

	OutSummary.Empty();
	if (OutFailures) OutFailures->Reset();

	if (!Blueprint)
	{
		OutSummary = TEXT("Blueprint 为空。");
		return false;
	}

	const FScopedTransaction Tx(NSLOCTEXT("BlueprintAIAssistant", "ExecuteDslSteps", "Blueprint AI Assistant: Execute DSL Steps"));

	const UBlueprintAIAssistantSettings* Settings = GetDefault<UBlueprintAIAssistantSettings>();
	const bool bAggregateFailures = Settings && Settings->bAggregateDslExecutionFailures;

	struct FOptionalExecBridgeState
	{
		FString InNodeId;
		FString InPin;
		FString OutNodeId;
		FString OutPin;
		bool bBridged = false;
	};

	int32 SuccessCount = 0;
	int32 SkippedCount = 0;
	TArray<FString> FailedLines;
	TArray<FString> SkippedLines;
	TSet<FString> SkippedOptionalNodeIds;
	TMap<FString, FOptionalExecBridgeState> OptionalExecBridgeByNode;

	auto TryBridgeOptionalExecNode = [&](const FString& SkippedNodeId, const FBlueprintDslActionStep& ContextStep) -> bool
	{
		FOptionalExecBridgeState* State = OptionalExecBridgeByNode.Find(SkippedNodeId);
		if (!State || State->bBridged)
		{
			return false;
		}
		if (State->InNodeId.IsEmpty() || State->OutNodeId.IsEmpty())
		{
			return false;
		}
		// 保守策略：暂不处理“可选节点链式跳过”的桥接，避免错误桥接。
		if (SkippedOptionalNodeIds.Contains(State->InNodeId) || SkippedOptionalNodeIds.Contains(State->OutNodeId))
		{
			return false;
		}

		FBlueprintDslActionStep Bridge;
		Bridge.ActionType = EBlueprintDslActionType::ConnectPins;
		Bridge.TargetGraph = ContextStep.TargetGraph;
		Bridge.FromNodeId = State->InNodeId;
		Bridge.FromPin = State->InPin.IsEmpty() ? TEXT("then") : State->InPin;
		Bridge.ToNodeId = State->OutNodeId;
		Bridge.ToPin = State->OutPin.IsEmpty() ? TEXT("execute") : State->OutPin;
		Bridge.Description = FString::Printf(TEXT("AutoBridgeOptional:%s"), *SkippedNodeId);

		FString BridgeErr;
		const bool bBridgeOk = ExecuteStep(Bridge, Blueprint, BridgeErr, nullptr);
		if (bBridgeOk)
		{
			State->bBridged = true;
			SkippedLines.Add(FString::Printf(
				TEXT("自动重连：因可选节点 %s 被跳过，已将 %s.%s -> %s.%s"),
				*SkippedNodeId,
				*Bridge.FromNodeId, *Bridge.FromPin,
				*Bridge.ToNodeId, *Bridge.ToPin));
			return true;
		}

		SkippedLines.Add(FString::Printf(
			TEXT("自动重连尝试失败（可选节点 %s）：%s"),
			*SkippedNodeId, *BridgeErr));
		return false;
	};

	for (int32 i = 0; i < Steps.Num(); ++i)
	{
		const FBlueprintDslActionStep& Cur = Steps[i];

		// 若步骤直接引用了“已跳过的可选节点”，则自动跳过，避免连锁失败。
		if (Cur.ActionType == EBlueprintDslActionType::ConnectPins &&
			(SkippedOptionalNodeIds.Contains(Cur.FromNodeId) || SkippedOptionalNodeIds.Contains(Cur.ToNodeId)))
		{
			// 记录“被跳过可选节点”的 Exec 入/出边，并在条件满足时自动桥接。
			const FString SkippedNodeId = SkippedOptionalNodeIds.Contains(Cur.FromNodeId) ? Cur.FromNodeId : Cur.ToNodeId;
			FOptionalExecBridgeState& BridgeState = OptionalExecBridgeByNode.FindOrAdd(SkippedNodeId);
			if (SkippedNodeId.Equals(Cur.ToNodeId, ESearchCase::CaseSensitive))
			{
				// 上游 -> [可选节点.execute]
				if (!IsOutputExecAlias(Cur.FromPin))
				{
					// 只桥接执行主链，数据线只跳过不重连。
				}
				else if (IsInputExecAlias(Cur.ToPin))
				{
					BridgeState.InNodeId = Cur.FromNodeId;
					BridgeState.InPin = Cur.FromPin;
					TryBridgeOptionalExecNode(SkippedNodeId, Cur);
				}
			}
			else
			{
				// [可选节点.then] -> 下游
				if (IsOutputExecAlias(Cur.FromPin) && IsInputExecAlias(Cur.ToPin))
				{
					BridgeState.OutNodeId = Cur.ToNodeId;
					BridgeState.OutPin = Cur.ToPin;
					TryBridgeOptionalExecNode(SkippedNodeId, Cur);
				}
			}

			++SkippedCount;
			SkippedLines.Add(FString::Printf(
				TEXT("第 %d 步已跳过：引用可选节点（%s -> %s）。"),
				i + 1, *Cur.FromNodeId, *Cur.ToNodeId));
			continue;
		}
		if (Cur.ActionType == EBlueprintDslActionType::SetPinDefault &&
			SkippedOptionalNodeIds.Contains(Cur.NodeId))
		{
			++SkippedCount;
			SkippedLines.Add(FString::Printf(
				TEXT("第 %d 步已跳过：可选节点 %s 不存在。"),
				i + 1, *Cur.NodeId));
			continue;
		}

		FString Error;
		FDslStepFailure StepFailure;
		StepFailure.StepIndex = i;
		const bool bOk = ExecuteStep(Cur, Blueprint, Error, &StepFailure);
		if (bOk)
		{
			++SuccessCount;
			continue;
		}

		// 可选函数节点不存在时：标记跳过并继续（不算失败，不中断批量）。
		const bool bOptionalMissingFunction =
			(Cur.ActionType == EBlueprintDslActionType::CreateNode) &&
			Cur.NodeType.Equals(TEXT("CallFunction"), ESearchCase::IgnoreCase) &&
			IsStepMarkedOptional(Cur) &&
			Error.Contains(TEXT("未找到函数"));
		if (bOptionalMissingFunction)
		{
			++SkippedCount;
			if (!Cur.NodeId.IsEmpty())
			{
				SkippedOptionalNodeIds.Add(Cur.NodeId);
				OptionalExecBridgeByNode.FindOrAdd(Cur.NodeId);
			}
			SkippedLines.Add(FString::Printf(
				TEXT("第 %d 步已跳过：可选函数 %s 不存在。"),
				i + 1, *Cur.FunctionName));
			continue;
		}

		// 确保 StepIndex 和 StepId 已填充（ExecuteStep 内也会填，这里补一次保险）
		StepFailure.StepIndex = i;
		if (StepFailure.StepId.IsEmpty())
		{
			StepFailure.StepId = Cur.StepId;
		}
		if (OutFailures)
		{
			OutFailures->Add(StepFailure);
		}

		const FString Line = FString::Printf(TEXT("第 %d 步失败：%s"), i + 1, *Error);
		FailedLines.Add(Line);

		if (!bAggregateFailures)
		{
			OutSummary += Line + TEXT("\n");
			OutSummary += FString::Printf(TEXT("已自动停止在第 %d 步。\n"), i + 1);
			break;
		}
	}

	// 轻量自动排版：尽量让本次生成的节点更整齐（不改变连接，仅移动节点）
	AutoLayoutSteps(Steps, Blueprint);

	if (bAggregateFailures)
	{
		if (SkippedLines.Num() > 0)
		{
			OutSummary += TEXT("已跳过（可选）步骤：\n");
			for (const FString& L : SkippedLines)
			{
				OutSummary += TEXT("- ") + L + TEXT("\n");
			}
		}
		if (FailedLines.Num() > 0)
		{
			OutSummary += TEXT("失败汇总：\n");
			for (const FString& L : FailedLines)
			{
				OutSummary += TEXT("- ") + L + TEXT("\n");
			}
		}
	}

	if (!bAggregateFailures && SkippedLines.Num() > 0)
	{
		OutSummary += TEXT("已跳过（可选）步骤：\n");
		for (const FString& L : SkippedLines)
		{
			OutSummary += TEXT("- ") + L + TEXT("\n");
		}
	}

	OutSummary = FString::Printf(TEXT("执行完成：成功 %d，跳过 %d，共 %d。\n%s"), SuccessCount, SkippedCount, Steps.Num(), *OutSummary);
	return FailedLines.Num() == 0;
}

static bool IsExecPin(const UEdGraphPin* P)
{
	return P && (P->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec);
}

static bool IsExecPinNameAlias(const FString& S)
{
	return IsInputExecAlias(S) || IsOutputExecAlias(S);
}

static bool StepDeclaresNodeId(const FBlueprintDslActionStep& Step)
{
	return Step.ActionType == EBlueprintDslActionType::CreateNode ||
		Step.ActionType == EBlueprintDslActionType::GetVariable ||
		Step.ActionType == EBlueprintDslActionType::SetVariable;
}

static void GatherNodesForLayout(UBlueprint* Blueprint, const TArray<FBlueprintDslActionStep>& Steps, TMap<UEdGraph*, TArray<UEdGraphNode*>>& OutGraphToNodes)
{
	OutGraphToNodes.Reset();
	if (!Blueprint)
	{
		return;
	}

	for (const FBlueprintDslActionStep& Step : Steps)
	{
		FString GraphError;
		UEdGraph* Graph = ResolveTargetGraphForStep(Blueprint, Step, GraphError);
		if (!Graph)
		{
			continue;
		}

		// 常规：根据 nodeId tag 找到本次创建/访问/设置变量的节点
		if (StepDeclaresNodeId(Step) && !Step.NodeId.IsEmpty())
		{
			if (UEdGraphNode* N = FindNodeByTaggedNodeId(Graph, Step.NodeId))
			{
				OutGraphToNodes.FindOrAdd(Graph).AddUnique(N);
			}
			continue;
		}

		// v3：Comment 节点跟随。Comment action 没有 nodeId tag，这里用“文本匹配 + 默认大小/位置”的弱特征把它纳入本次簇。
		if (Step.ActionType == EBlueprintDslActionType::Comment && !Step.CommentText.IsEmpty())
		{
			const FString Want = Step.CommentText.Left(2000);
			for (UEdGraphNode* N : Graph->Nodes)
			{
				UEdGraphNode_Comment* C = Cast<UEdGraphNode_Comment>(N);
				if (!C)
				{
					continue;
				}
				if (C->NodeComment != Want)
				{
					continue;
				}
				// ExecuteComment 的默认值：200,200 + 700x200（尽量降低误选老 comment 的概率）
				if (C->NodePosX == 200 && C->NodePosY == 200 && C->NodeWidth == 700 && C->NodeHeight == 200)
				{
					OutGraphToNodes.FindOrAdd(Graph).AddUnique(C);
				}
			}
		}
	}
}

/** 一条排版用的 exec 邻接边：从 FromNode 的「输出 exec pin」连向 To。FromPinIndex 用于子节点排序（越小 = 源节点上越靠上）。 */
struct FLayoutExecEdge
{
	UEdGraphNode* To = nullptr;
	int32 FromPinIndex = 0;
};

static void BuildExecEdges(UBlueprint* Blueprint, const TArray<FBlueprintDslActionStep>& Steps, UEdGraph* Graph, const TSet<UEdGraphNode*>& NodesInScope, TMap<UEdGraphNode*, TArray<FLayoutExecEdge>>& OutFromTo)
{
	OutFromTo.Reset();
	if (!Blueprint || !Graph)
	{
		return;
	}

	for (const FBlueprintDslActionStep& Step : Steps)
	{
		if (Step.ActionType != EBlueprintDslActionType::ConnectPins)
		{
			continue;
		}
		if (Step.FromNodeId.IsEmpty() || Step.ToNodeId.IsEmpty())
		{
			continue;
		}

		FString GraphError;
		UEdGraph* StepGraph = ResolveTargetGraphForStep(Blueprint, Step, GraphError);
		if (StepGraph != Graph)
		{
			continue;
		}

		UEdGraphNode* FromNode = FindNodeByNodeIdOrSpecial(Blueprint, Graph, Step.FromNodeId);
		UEdGraphNode* ToNode = FindNodeByNodeIdOrSpecial(Blueprint, Graph, Step.ToNodeId);
		if (!FromNode || !ToNode)
		{
			continue;
		}

		// 只处理“看起来是执行线”的 ConnectPins
		if (!IsExecPinNameAlias(Step.FromPin) && !IsExecPinNameAlias(Step.ToPin))
		{
			continue;
		}

		UEdGraphPin* FP = FindPinLoose(FromNode, Step.FromPin);
		UEdGraphPin* TP = FindPinLoose(ToNode, Step.ToPin);
		if (!FP && IsExecPinNameAlias(Step.FromPin))
		{
			FP = FindFirstExecPinByDirection(FromNode, EGPD_Output);
		}
		if (!TP && IsExecPinNameAlias(Step.ToPin))
		{
			TP = FindFirstExecPinByDirection(ToNode, EGPD_Input);
		}
		if (!IsExecPin(FP) || !IsExecPin(TP))
		{
			continue;
		}

		// 仅把“本次步骤声明/涉及”的节点纳入主布局；特殊事件根（BeginPlay/AnyDamage/InputKey 等）允许作为根
		if (!NodesInScope.Contains(ToNode))
		{
			continue;
		}
		if (!NodesInScope.Contains(FromNode) &&
			!IsBeginPlayNodeId(Step.FromNodeId) &&
			!IsAnyDamageNodeId(Step.FromNodeId) &&
			!IsActorBeginOverlapNodeId(Step.FromNodeId) &&
			!IsActorEndOverlapNodeId(Step.FromNodeId) &&
			!IsComponentBeginOverlapNodeId(Step.FromNodeId) &&
			!IsInputKeyNodeId(Step.FromNodeId))
		{
			continue;
		}

		FLayoutExecEdge Edge;
		Edge.To = ToNode;
		Edge.FromPinIndex = FromNode->Pins.IndexOfByKey(FP);
		if (Edge.FromPinIndex == INDEX_NONE)
		{
			Edge.FromPinIndex = 0;
		}
		OutFromTo.FindOrAdd(FromNode).Add(Edge);
	}
}

/**
 * 若该蓝图的 Blueprint 编辑器已打开：在对应图上选中本次排版涉及的节点并调用编辑器「拉直连接线」
 *（与菜单 Straighten Connections 同源）。未打开编辑器或无法取得 SGraphEditor 时静默跳过。
 */
static void TryEditorStraightenConnectionsForLayoutGraphs(UBlueprint* Blueprint, const TMap<UEdGraph*, TArray<UEdGraphNode*>>& GraphToNodes)
{
	if (!Blueprint || GraphToNodes.Num() == 0)
	{
		return;
	}

	TSharedPtr<IBlueprintEditor> BpEditor = FKismetEditorUtilities::GetIBlueprintEditorForObject(Blueprint, /*bOpenEditor*/ false);
	if (!BpEditor.IsValid())
	{
		return;
	}

	for (const TPair<UEdGraph*, TArray<UEdGraphNode*>>& Pair : GraphToNodes)
	{
		UEdGraph* Graph = Pair.Key;
		const TArray<UEdGraphNode*>& Nodes = Pair.Value;
		if (!Graph || Nodes.Num() == 0)
		{
			continue;
		}

		TSharedPtr<SGraphEditor> GraphEd = BpEditor->OpenGraphAndBringToFront(Graph, true);
		if (!GraphEd.IsValid())
		{
			continue;
		}

		// v3：分簇 Straighten（按 exec 连通分量）。比一次性选中整簇更稳定，也避免 comment 参与 straighten。
		TSet<UEdGraphNode*> NodeSet;
		for (UEdGraphNode* N : Nodes)
		{
			if (N)
			{
				NodeSet.Add(N);
			}
		}

		TArray<TArray<UEdGraphNode*>> Clusters;
		{
			TSet<UEdGraphNode*> Visited;
			for (UEdGraphNode* Seed : Nodes)
			{
				if (!Seed || Visited.Contains(Seed) || Cast<UEdGraphNode_Comment>(Seed))
				{
					continue;
				}

				TArray<UEdGraphNode*> Cluster;
				TArray<UEdGraphNode*> Stack;
				Stack.Add(Seed);
				Visited.Add(Seed);

				while (Stack.Num() > 0)
				{
					UEdGraphNode* Cur = Stack.Pop(EAllowShrinking::No);
					if (!Cur)
					{
						continue;
					}
					Cluster.Add(Cur);

					for (UEdGraphPin* P : Cur->Pins)
					{
						if (!IsExecPin(P))
						{
							continue;
						}
						for (UEdGraphPin* L : P->LinkedTo)
						{
							UEdGraphNode* Other = L ? L->GetOwningNode() : nullptr;
							if (!Other || !NodeSet.Contains(Other) || Visited.Contains(Other) || Cast<UEdGraphNode_Comment>(Other))
							{
								continue;
							}
							Visited.Add(Other);
							Stack.Add(Other);
						}
					}
				}

				if (Cluster.Num() > 0)
				{
					Clusters.Add(MoveTemp(Cluster));
				}
			}
		}

		// 若没有 exec 连通分量（全是纯数据/注释），退化为一次性 straighten（会被 K2 忽略/无副作用）
		if (Clusters.Num() == 0)
		{
			Clusters.Add(Nodes);
		}

		for (const TArray<UEdGraphNode*>& Cluster : Clusters)
		{
			GraphEd->ClearSelectionSet();
			for (UEdGraphNode* N : Cluster)
			{
				if (N && !Cast<UEdGraphNode_Comment>(N))
				{
					GraphEd->SetNodeSelection(N, true);
				}
			}
			GraphEd->OnStraightenConnections();
		}
	}
}

/** 取得节点的估算尺寸（graph 空间下的宽高）。优先使用 Slate 缓存的真实尺寸，其次 NodeWidth/Height，最后经验常量。 */
static FVector2f MeasureNodeSize(TSharedPtr<SGraphEditor> GraphEd, UEdGraphNode* Node)
{
	const FVector2f FallbackSize(240.f, 120.f);

	if (!Node)
	{
		return FallbackSize;
	}

	if (GraphEd.IsValid())
	{
		FSlateRect Rect;
		if (GraphEd->GetBoundsForNode(Node, Rect, 0.f))
		{
			const float W = Rect.Right - Rect.Left;
			const float H = Rect.Bottom - Rect.Top;
			if (W > 1.f && H > 1.f)
			{
				return FVector2f(W, H);
			}
		}
	}

	if (Node->NodeWidth > 0 && Node->NodeHeight > 0)
	{
		return FVector2f(static_cast<float>(Node->NodeWidth), static_cast<float>(Node->NodeHeight));
	}

	return FallbackSize;
}

static FSlateRect MeasureNodeRect(TSharedPtr<SGraphEditor> GraphEd, UEdGraphNode* Node)
{
	if (!Node)
	{
		return FSlateRect(0.f, 0.f, 0.f, 0.f);
	}
	const FVector2f Sz = MeasureNodeSize(GraphEd, Node);
	const float L = static_cast<float>(Node->NodePosX);
	const float T = static_cast<float>(Node->NodePosY);
	return FSlateRect(L, T, L + Sz.X, T + Sz.Y);
}

static bool SegmentIntersectsRect(const FVector2f& A, const FVector2f& B, const FSlateRect& R)
{
	const float MinX = FMath::Min(A.X, B.X);
	const float MaxX = FMath::Max(A.X, B.X);
	const float MinY = FMath::Min(A.Y, B.Y);
	const float MaxY = FMath::Max(A.Y, B.Y);
	if (MaxX < R.Left || MinX > R.Right || MaxY < R.Top || MinY > R.Bottom)
	{
		return false;
	}

	const int32 Samples = 9;
	for (int32 i = 0; i <= Samples; ++i)
	{
		const float Alpha = static_cast<float>(i) / static_cast<float>(Samples);
		const FVector2f P = FMath::Lerp(A, B, Alpha);
		if (P.X >= R.Left && P.X <= R.Right && P.Y >= R.Top && P.Y <= R.Bottom)
		{
			return true;
		}
	}
	return false;
}

static bool IsPureDataNode(const UEdGraphNode* Node)
{
	if (!Node)
	{
		return false;
	}
	for (const UEdGraphPin* P : Node->Pins)
	{
		if (IsExecPin(P))
		{
			return false;
		}
	}
	return true;
}

static void ApplyDataSatelliteLayout(UEdGraph* Graph, const TArray<UEdGraphNode*>& Nodes, const TSet<UEdGraphNode*>& NodeSet, TSharedPtr<SGraphEditor> GraphEd)
{
	// v3：纯数据节点贴靠使用点（只移动本次簇内的纯节点；不改连线）
	if (!Graph || Nodes.Num() == 0)
	{
		return;
	}

	const float SidePadX = 120.f;
	const float SidePadY = 40.f;

	for (UEdGraphNode* N : Nodes)
	{
		if (!N || !NodeSet.Contains(N))
		{
			continue;
		}
		if (!IsPureDataNode(N) || Cast<UEdGraphNode_Comment>(N) || Cast<UK2Node_Knot>(N))
		{
			continue;
		}

		UEdGraphNode* BestConsumer = nullptr;
		float BestScore = FLT_MAX;

		for (UEdGraphPin* P : N->Pins)
		{
			if (!P)
			{
				continue;
			}
			for (UEdGraphPin* L : P->LinkedTo)
			{
				UEdGraphNode* Other = L ? L->GetOwningNode() : nullptr;
				if (!Other || !NodeSet.Contains(Other))
				{
					continue;
				}
				if (IsPureDataNode(Other) || Cast<UEdGraphNode_Comment>(Other))
				{
					continue;
				}
				const float Dx = static_cast<float>(Other->NodePosX - N->NodePosX);
				const float Dy = static_cast<float>(Other->NodePosY - N->NodePosY);
				const float Score = FMath::Abs(Dx) + 0.5f * FMath::Abs(Dy);
				if (Score < BestScore)
				{
					BestScore = Score;
					BestConsumer = Other;
				}
			}
		}

		if (!BestConsumer)
		{
			continue;
		}

		const FVector2f NSize = MeasureNodeSize(GraphEd, N);
		const FVector2f CSize = MeasureNodeSize(GraphEd, BestConsumer);

		const int32 TargetX = BestConsumer->NodePosX - static_cast<int32>(NSize.X) - static_cast<int32>(SidePadX);
		const int32 TargetY = BestConsumer->NodePosY + static_cast<int32>(0.2f * CSize.Y) + static_cast<int32>(SidePadY);

		N->Modify();
		N->NodePosX = TargetX;
		N->NodePosY = TargetY;
		N->SnapToGrid(16);
		Graph->NotifyNodeChanged(N);
	}
}

static void ApplyCommentFollowLayout(UEdGraph* Graph, const TArray<UEdGraphNode*>& Nodes, const TSet<UEdGraphNode*>& NodeSet, TSharedPtr<SGraphEditor> GraphEd)
{
	// v3：让 comment 包住本次簇（弱规则：只处理 NodeSet 内 comment）
	if (!Graph || Nodes.Num() == 0)
	{
		return;
	}

	TArray<UEdGraphNode_Comment*> Comments;
	TArray<UEdGraphNode*> Body;
	for (UEdGraphNode* N : Nodes)
	{
		if (!N || !NodeSet.Contains(N))
		{
			continue;
		}
		if (UEdGraphNode_Comment* C = Cast<UEdGraphNode_Comment>(N))
		{
			Comments.Add(C);
		}
		else
		{
			Body.Add(N);
		}
	}
	if (Comments.Num() == 0 || Body.Num() == 0)
	{
		return;
	}

	FSlateRect Bounds(FLT_MAX, FLT_MAX, -FLT_MAX, -FLT_MAX);
	for (UEdGraphNode* N : Body)
	{
		const FSlateRect R = MeasureNodeRect(GraphEd, N);
		Bounds.Left = FMath::Min(Bounds.Left, R.Left);
		Bounds.Top = FMath::Min(Bounds.Top, R.Top);
		Bounds.Right = FMath::Max(Bounds.Right, R.Right);
		Bounds.Bottom = FMath::Max(Bounds.Bottom, R.Bottom);
	}

	const float Pad = 80.f;
	Bounds.Left -= Pad;
	Bounds.Top -= Pad;
	Bounds.Right += Pad;
	Bounds.Bottom += Pad;

	for (UEdGraphNode_Comment* C : Comments)
	{
		if (!C)
		{
			continue;
		}

		// v3：只扩不缩。若用户手动把 comment 拉大，则保持其大小；只有当包围盒不够用时才扩张到 union。
		const float CurL = static_cast<float>(C->NodePosX);
		const float CurT = static_cast<float>(C->NodePosY);
		const float CurR = CurL + static_cast<float>(C->NodeWidth);
		const float CurB = CurT + static_cast<float>(C->NodeHeight);

		const bool bContains = (CurL <= Bounds.Left && CurT <= Bounds.Top && CurR >= Bounds.Right && CurB >= Bounds.Bottom);
		FSlateRect Final = Bounds;
		if (!bContains)
		{
			Final.Left = FMath::Min(CurL, Bounds.Left);
			Final.Top = FMath::Min(CurT, Bounds.Top);
			Final.Right = FMath::Max(CurR, Bounds.Right);
			Final.Bottom = FMath::Max(CurB, Bounds.Bottom);
		}

		C->Modify();
		C->NodePosX = FMath::RoundToInt(Final.Left);
		C->NodePosY = FMath::RoundToInt(Final.Top);
		C->NodeWidth = FMath::RoundToInt(FMath::Max(300.f, Final.Right - Final.Left));
		C->NodeHeight = FMath::RoundToInt(FMath::Max(200.f, Final.Bottom - Final.Top));
		C->SnapToGrid(16);
		Graph->NotifyNodeChanged(C);
	}
}

static void InsertRerouteKnotBetweenPins(UEdGraph* Graph, UEdGraphPin* FromPin, UEdGraphPin* ToPin, const FVector2f& KnotPos, UK2Node_Knot*& OutKnot)
{
	OutKnot = nullptr;
	if (!Graph || !FromPin || !ToPin)
	{
		return;
	}

	const UEdGraphSchema_K2* Schema = GetDefault<UEdGraphSchema_K2>();
	if (!Schema)
	{
		return;
	}

	FromPin->BreakLinkTo(ToPin);

	UK2Node_Knot* Knot = NewObject<UK2Node_Knot>(Graph);
	Graph->AddNode(Knot, true, false);
	Knot->CreateNewGuid();
	Knot->AllocateDefaultPins();
	Knot->PostPlacedNewNode();
	Knot->Modify();
	Knot->NodePosX = FMath::RoundToInt(KnotPos.X);
	Knot->NodePosY = FMath::RoundToInt(KnotPos.Y);
	Knot->SnapToGrid(16);
	Graph->NotifyNodeChanged(Knot);

	UEdGraphPin* KnotIn = Knot->GetInputPin();
	UEdGraphPin* KnotOut = Knot->GetOutputPin();
	if (!KnotIn || !KnotOut)
	{
		return;
	}

	Schema->TryCreateConnection(FromPin, KnotIn);
	Schema->TryCreateConnection(KnotOut, ToPin);
	OutKnot = Knot;
}

static void ApplyExecLongWireKnotting(UEdGraph* Graph, const TSet<UEdGraphNode*>& NodeSet, TSharedPtr<SGraphEditor> GraphEd, TArray<UEdGraphNode*>& InOutNodes)
{
	// v3：长 exec 线穿越节点时，插入 Knot（Reroute）
	if (!Graph || NodeSet.Num() == 0)
	{
		return;
	}

	TArray<UEdGraphNode*> Obstacles;
	TArray<FSlateRect> ObRects;
	for (UEdGraphNode* N : Graph->Nodes)
	{
		if (!N || !NodeSet.Contains(N))
		{
			continue;
		}
		if (Cast<UEdGraphNode_Comment>(N) || Cast<UK2Node_Knot>(N))
		{
			continue;
		}
		Obstacles.Add(N);
		ObRects.Add(MeasureNodeRect(GraphEd, N));
	}

	const float LongDx = 420.f;

	for (UEdGraphNode* FromNode : Obstacles)
	{
		if (!FromNode)
		{
			continue;
		}
		for (UEdGraphPin* P : FromNode->Pins)
		{
			if (!IsExecPin(P) || P->Direction != EGPD_Output)
			{
				continue;
			}
			for (int32 li = P->LinkedTo.Num() - 1; li >= 0; --li)
			{
				UEdGraphPin* ToPin = P->LinkedTo.IsValidIndex(li) ? P->LinkedTo[li] : nullptr;
				UEdGraphNode* ToNode = ToPin ? ToPin->GetOwningNode() : nullptr;
				if (!ToPin || !ToNode || !NodeSet.Contains(ToNode))
				{
					continue;
				}
				if (!IsExecPin(ToPin) || ToPin->Direction != EGPD_Input)
				{
					continue;
				}
				if (Cast<UK2Node_Knot>(ToNode))
				{
					continue;
				}

				const FVector2f A(static_cast<float>(FromNode->NodePosX), static_cast<float>(FromNode->NodePosY));
				const FVector2f B(static_cast<float>(ToNode->NodePosX), static_cast<float>(ToNode->NodePosY));
				if (FMath::Abs(B.X - A.X) < LongDx)
				{
					continue;
				}

				bool bHits = false;
				for (int32 oi = 0; oi < Obstacles.Num(); ++oi)
				{
					UEdGraphNode* Ob = Obstacles[oi];
					if (!Ob || Ob == FromNode || Ob == ToNode)
					{
						continue;
					}
					if (SegmentIntersectsRect(A, B, ObRects[oi]))
					{
						bHits = true;
						break;
					}
				}
				if (!bHits)
				{
					continue;
				}

				const FVector2f Mid = (A + B) * 0.5f;
				UK2Node_Knot* NewKnot = nullptr;
				InsertRerouteKnotBetweenPins(Graph, P, ToPin, Mid, NewKnot);
				if (NewKnot)
				{
					InOutNodes.AddUnique(NewKnot);
				}
			}
		}
	}
}

/** 对本次排版结果外的既有节点做碰撞避让：被「本次节点 bbox」盖住的既有节点整体下移避让；级联上限 5 次。 */
static void AvoidCollisionsWithExistingNodes(UEdGraph* Graph, const TArray<UEdGraphNode*>& LaidOutNodes, const TSet<UEdGraphNode*>& NodeSet, TSharedPtr<SGraphEditor> GraphEd)
{
	if (!Graph || LaidOutNodes.Num() == 0)
	{
		return;
	}

	const float VertPad = 32.f;

	struct FRect { float L, T, R, B; UEdGraphNode* N; };

	auto MakeRect = [&](UEdGraphNode* N) -> FRect
	{
		const FVector2f Size = MeasureNodeSize(GraphEd, N);
		return { (float)N->NodePosX, (float)N->NodePosY, (float)N->NodePosX + Size.X, (float)N->NodePosY + Size.Y, N };
	};

	auto Overlaps = [](const FRect& A, const FRect& B) -> bool
	{
		return !(A.R <= B.L || A.L >= B.R || A.B <= B.T || A.T >= B.B);
	};

	// 收集本次节点 Rects 与图内其他节点 Rects。
	TArray<FRect> OurRects;
	for (UEdGraphNode* N : LaidOutNodes)
	{
		if (N) { OurRects.Add(MakeRect(N)); }
	}

	TArray<FRect> OtherRects;
	for (UEdGraphNode* N : Graph->Nodes)
	{
		if (!N || NodeSet.Contains(N))
		{
			continue;
		}
		OtherRects.Add(MakeRect(N));
	}

	// 每轮：若任一 OurRect 与 OtherRect 重叠，把被挡的 Other 整体下移到空出位置；级联下移上限 5 次。
	const int32 MaxPass = 5;
	for (int32 Pass = 0; Pass < MaxPass; ++Pass)
	{
		bool bMoved = false;
		for (FRect& Other : OtherRects)
		{
			for (const FRect& Ours : OurRects)
			{
				if (Overlaps(Ours, Other))
				{
					const float Delta = (Ours.B - Other.T) + VertPad;
					if (Delta > 0.f)
					{
						if (Other.N)
						{
							Other.N->Modify();
							Other.N->NodePosY += FMath::CeilToInt(Delta);
							Other.N->SnapToGrid(16);
							Graph->NotifyNodeChanged(Other.N);
						}
						Other.T += Delta;
						Other.B += Delta;
						bMoved = true;
					}
				}
			}
		}
		if (!bMoved)
		{
			break;
		}

		// 级联：Others 内部互相冲突的按 Y 再推一次
		OtherRects.Sort([](const FRect& A, const FRect& B) { return A.T < B.T; });
		for (int32 i = 0; i < OtherRects.Num(); ++i)
		{
			for (int32 j = i + 1; j < OtherRects.Num(); ++j)
			{
				if (Overlaps(OtherRects[i], OtherRects[j]))
				{
					const float Delta = (OtherRects[i].B - OtherRects[j].T) + VertPad;
					if (Delta > 0.f && OtherRects[j].N)
					{
						OtherRects[j].N->Modify();
						OtherRects[j].N->NodePosY += FMath::CeilToInt(Delta);
						OtherRects[j].N->SnapToGrid(16);
						Graph->NotifyNodeChanged(OtherRects[j].N);
						OtherRects[j].T += Delta;
						OtherRects[j].B += Delta;
					}
				}
			}
		}
	}
}

static void LayoutExecGraph(UEdGraph* Graph, const TArray<UEdGraphNode*>& Nodes, const TMap<UEdGraphNode*, TArray<FLayoutExecEdge>>& Edges, TSharedPtr<SGraphEditor> GraphEd)
{
	if (!Graph || Nodes.Num() == 0)
	{
		return;
	}

	// 计算入度（只对 Nodes 内计算；BeginPlay 作为虚拟根允许出现）
	TMap<UEdGraphNode*, int32> InDegree;
	for (UEdGraphNode* N : Nodes)
	{
		InDegree.Add(N, 0);
	}
	for (const TPair<UEdGraphNode*, TArray<FLayoutExecEdge>>& Pair : Edges)
	{
		for (const FLayoutExecEdge& E : Pair.Value)
		{
			if (InDegree.Contains(E.To))
			{
				InDegree[E.To] += 1;
			}
		}
	}

	// 根：入度为 0 的节点
	TArray<UEdGraphNode*> Roots;
	for (const auto& Kvp : InDegree)
	{
		if (Kvp.Value == 0)
		{
			Roots.Add(Kvp.Key);
		}
	}
	if (Roots.Num() == 0)
	{
		Roots = Nodes;
	}

	TMap<UEdGraphNode*, int32> Col;
	TMap<UEdGraphNode*, int32> Row;

	int32 NextRootRow = 0;
	TQueue<UEdGraphNode*> Q;
	for (UEdGraphNode* R : Roots)
	{
		Col.FindOrAdd(R) = 0;
		Row.FindOrAdd(R) = NextRootRow++;
		Q.Enqueue(R);
	}

	while (!Q.IsEmpty())
	{
		UEdGraphNode* Cur = nullptr;
		Q.Dequeue(Cur);
		if (!Cur)
		{
			continue;
		}
		const int32 CurCol = Col.FindRef(Cur);
		const int32 CurRow = Row.FindRef(Cur);

		const TArray<FLayoutExecEdge>* SuccPtr = Edges.Find(Cur);
		if (!SuccPtr)
		{
			continue;
		}

		// v2：子节点排序从「NodePosY」改为「源节点上的 exec pin index」，让 Branch 的 True 在上、False 在下，Sequence Then0..N 自上而下。
		TArray<FLayoutExecEdge> Succ = *SuccPtr;
		Succ.Sort([](const FLayoutExecEdge& A, const FLayoutExecEdge& B) { return A.FromPinIndex < B.FromPinIndex; });

		for (int32 i = 0; i < Succ.Num(); ++i)
		{
			UEdGraphNode* S = Succ[i].To;
			if (!S)
			{
				continue;
			}
			const int32 DesiredCol = CurCol + 1;
			const int32 DesiredRow = (i == 0) ? CurRow : (CurRow + i);

			bool bUpdated = false;
			if (!Col.Contains(S) || DesiredCol > Col[S])
			{
				Col.Add(S, DesiredCol);
				bUpdated = true;
			}
			if (!Row.Contains(S))
			{
				Row.Add(S, DesiredRow);
				bUpdated = true;
			}

			if (bUpdated)
			{
				Q.Enqueue(S);
			}
		}
	}

	// v2：真实尺寸度量 + 按列宽 / 行高 的累计坐标（避免固定 StepX/Y 造成节点叠加 or 空旷）。
	const float BaseX = 240.f;
	const float BaseY = 180.f;
	const float HorizPad = 80.f;
	const float VertPad = 40.f;

	// 每列最大宽、每行最大高
	TMap<int32, float> ColWidth;
	TMap<int32, float> RowHeight;

	TMap<UEdGraphNode*, FVector2f> SizeCache;
	for (UEdGraphNode* N : Nodes)
	{
		if (!N) { continue; }
		const FVector2f Sz = MeasureNodeSize(GraphEd, N);
		SizeCache.Add(N, Sz);

		const int32 C = Col.Contains(N) ? Col[N] : 0;
		const int32 R = Row.Contains(N) ? Row[N] : 0;

		float& CW = ColWidth.FindOrAdd(C, 0.f);
		CW = FMath::Max(CW, Sz.X);

		float& RH = RowHeight.FindOrAdd(R, 0.f);
		RH = FMath::Max(RH, Sz.Y);
	}

	// 列 X / 行 Y 的累计位置
	TArray<int32> ColKeys; ColWidth.GenerateKeyArray(ColKeys); ColKeys.Sort();
	TArray<int32> RowKeys; RowHeight.GenerateKeyArray(RowKeys); RowKeys.Sort();

	TMap<int32, float> ColX;
	{
		float X = BaseX;
		int32 LastKey = INT32_MIN;
		for (int32 Key : ColKeys)
		{
			if (LastKey != INT32_MIN)
			{
				const float PrevW = ColWidth.FindRef(LastKey);
				X += PrevW + HorizPad;
			}
			ColX.Add(Key, X);
			LastKey = Key;
		}
	}

	TMap<int32, float> RowY;
	{
		float Y = BaseY;
		int32 LastKey = INT32_MIN;
		for (int32 Key : RowKeys)
		{
			if (LastKey != INT32_MIN)
			{
				const float PrevH = RowHeight.FindRef(LastKey);
				Y += PrevH + VertPad;
			}
			RowY.Add(Key, Y);
			LastKey = Key;
		}
	}

	for (UEdGraphNode* N : Nodes)
	{
		if (!N)
		{
			continue;
		}
		const int32 C = Col.Contains(N) ? Col[N] : 0;
		const int32 R = Row.Contains(N) ? Row[N] : 0;

		N->Modify();
		N->NodePosX = FMath::RoundToInt(ColX.FindRef(C));
		N->NodePosY = FMath::RoundToInt(RowY.FindRef(R));
		N->SnapToGrid(16);
		Graph->NotifyNodeChanged(N);
	}
}

void FBlueprintDslExecutor::AutoLayoutSteps(const TArray<FBlueprintDslActionStep>& Steps, UBlueprint* Blueprint)
{
	if (!Blueprint || Steps.Num() == 0)
	{
		return;
	}

	const UBlueprintAIAssistantSettings* Settings = GetDefault<UBlueprintAIAssistantSettings>();
	if (Settings && !Settings->bAutoLayoutAfterExecute)
	{
		return;
	}

	TMap<UEdGraph*, TArray<UEdGraphNode*>> GraphToNodes;
	GatherNodesForLayout(Blueprint, Steps, GraphToNodes);

	// 打开的蓝图编辑器（若有）。拿到后就能按图获取 SGraphEditor，用于真实尺寸度量与最终 Straighten。
	TSharedPtr<IBlueprintEditor> BpEditor = FKismetEditorUtilities::GetIBlueprintEditorForObject(Blueprint, /*bOpenEditor*/ false);

	for (auto& Pair : GraphToNodes)
	{
		UEdGraph* Graph = Pair.Key;
		TArray<UEdGraphNode*>& Nodes = Pair.Value;
		if (!Graph || Nodes.Num() == 0)
		{
			continue;
		}

		TSet<UEdGraphNode*> NodeSet;
		for (UEdGraphNode* N : Nodes)
		{
			if (N)
			{
				NodeSet.Add(N);
			}
		}

		TMap<UEdGraphNode*, TArray<FLayoutExecEdge>> ExecEdges;
		BuildExecEdges(Blueprint, Steps, Graph, NodeSet, ExecEdges);

		// 仅在编辑器已打开时才能拿到 Slate 真实尺寸；拿不到时 MeasureNodeSize 会自动兜底。
		TSharedPtr<SGraphEditor> GraphEd;
		if (BpEditor.IsValid())
		{
			GraphEd = BpEditor->OpenGraphAndBringToFront(Graph, /*bSetFocus*/ false);
		}

		LayoutExecGraph(Graph, Nodes, ExecEdges, GraphEd);

		// v3：纯数据节点贴靠使用点（减少数据线横穿主干）
		if (!Settings || Settings->bLayoutAttachDataNodesAsSatellites)
		{
			ApplyDataSatelliteLayout(Graph, Nodes, NodeSet, GraphEd);
		}

		// v3：长 exec 线穿节点 → 插入 Knot（Reroute）。新增节点也要纳入本次簇，便于后续避让与 Straighten。
		if (!Settings || Settings->bLayoutInsertKnotsForLongWires)
		{
			ApplyExecLongWireKnotting(Graph, NodeSet, GraphEd, Nodes);
			for (UEdGraphNode* N : Nodes)
			{
				if (N)
				{
					NodeSet.Add(N);
				}
			}
		}

		// v3：Comment 包围本次簇
		if (!Settings || Settings->bLayoutAutoResizeCommentBoxes)
		{
			ApplyCommentFollowLayout(Graph, Nodes, NodeSet, GraphEd);
		}

		// v2：碰撞避让（避免压住已存在的节点）
		if (Settings && Settings->bLayoutAvoidCollisions)
		{
			AvoidCollisionsWithExistingNodes(Graph, Nodes, NodeSet, GraphEd);
		}
	}

	FBlueprintEditorUtils::MarkBlueprintAsModified(Blueprint);

	// 编辑器已打开时尽量用原生「拉直连接线」整理线形；否则仅保留上方规则布局。
	TryEditorStraightenConnectionsForLayoutGraphs(Blueprint, GraphToNodes);
}

