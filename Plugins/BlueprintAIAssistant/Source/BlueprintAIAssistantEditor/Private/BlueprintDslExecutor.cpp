// 把常见用户/LLM 写法映射为 Blueprint 里真实的 UFunction 名。
// 注意：很多蓝图节点的"显示名"和底层 UFUNCTION 名不一致，例如
//   显示 "Line Trace By Channel" -> UFUNCTION 实际是 LineTraceSingle（接受 ETraceTypeQuery）
//   显示 "Sphere Trace By Channel" -> UFUNCTION 实际是 SphereTraceSingle
static FString ResolveFunctionNameAlias(const FString& FuncName)
{
	static const TMap<FString, FString> Alias = {
		// Actor 系列（蓝图里显示名 vs UFUNCTION 名）
		{ TEXT("GetActorLocation"),              TEXT("K2_GetActorLocation") },
		{ TEXT("GetActorRotation"),              TEXT("K2_GetActorRotation") },
		{ TEXT("GetActorScale3D"),               TEXT("GetActorScale3D") },
		{ TEXT("SetActorLocation"),              TEXT("K2_SetActorLocation") },
		{ TEXT("SetActorRotation"),              TEXT("K2_SetActorRotation") },
		{ TEXT("SetActorTransform"),             TEXT("K2_SetActorTransform") },
		{ TEXT("AddActorWorldOffset"),           TEXT("K2_AddActorWorldOffset") },
		{ TEXT("AddActorLocalOffset"),           TEXT("K2_AddActorLocalOffset") },
		{ TEXT("AttachToActor"),                 TEXT("K2_AttachToActor") },
		{ TEXT("AttachToComponent"),             TEXT("K2_AttachToComponent") },
		{ TEXT("DestroyActor"),                  TEXT("K2_DestroyActor") },

		// Trace 族：LLM 常按显示名（带 ByChannel 后缀）输出，归一到真实 UFUNCTION
		{ TEXT("LineTraceSingleByChannel"),      TEXT("LineTraceSingle") },
		{ TEXT("LineTraceByChannel"),            TEXT("LineTraceSingle") },
		{ TEXT("SphereTraceSingleByChannel"),    TEXT("SphereTraceSingle") },
		{ TEXT("SphereTraceByChannel"),          TEXT("SphereTraceSingle") },
		// 保留 ForObjects / ByProfile 变体（这些 UFUNCTION 名和显示名一致）
		{ TEXT("LineTraceForObjects"),           TEXT("LineTraceSingleForObjects") },
		{ TEXT("SphereTraceForObjects"),         TEXT("SphereTraceSingleForObjects") },
		{ TEXT("LineTraceByProfile"),            TEXT("LineTraceSingleByProfile") },
		{ TEXT("SphereTraceByProfile"),          TEXT("SphereTraceSingleByProfile") },

		// 常见运算节点显示名（蓝图面板）→ 真实 UFUNCTION
		// 反馈：LLM 常输出 "Equal (Int)" / "Add (Float)" 这类 display name
		{ TEXT("Equal (Int)"),                   TEXT("EqualEqual_IntInt") },
		{ TEXT("Equal(Int)"),                    TEXT("EqualEqual_IntInt") },
		{ TEXT("Add (Float)"),                   TEXT("Add_FloatFloat") },
		{ TEXT("Add(Float)"),                    TEXT("Add_FloatFloat") },
		{ TEXT("Int + Int"),                     TEXT("Add_IntInt") },
		{ TEXT("Int+Int"),                       TEXT("Add_IntInt") },
		{ TEXT("Add (Int)"),                     TEXT("Add_IntInt") },
		{ TEXT("Add(Int)"),                      TEXT("Add_IntInt") },
		{ TEXT("Float - Float"),                 TEXT("Subtract_FloatFloat") },
		{ TEXT("Float-Float"),                   TEXT("Subtract_FloatFloat") },

		// 组件
		{ TEXT("Get Component by Class"),        TEXT("GetComponentByClass") },
		{ TEXT("GetComponentByClass"),           TEXT("GetComponentByClass") },
		{ TEXT("Get Component By Class"),        TEXT("GetComponentByClass") },

		// Timer
		{ TEXT("Set Timer by Function Name"),    TEXT("K2_SetTimer") },
		{ TEXT("SetTimerByFunctionName"),       TEXT("K2_SetTimer") },
		{ TEXT("Set Timer For Next Tick by Function Name"), TEXT("K2_SetTimerForNextTick") },
	};

	const FString Trimmed = FuncName.TrimStartAndEnd();
	if (const FString* Mapped = Alias.Find(Trimmed))
	{
		return *Mapped;
	}

	// 兜底：对蓝图显示名做轻归一化（去空格/括号），避免不可见字符或空格差异导致 miss
	auto NormalizeDisplay = [](FString In) -> FString
	{
		In.TrimStartAndEndInline();
		In.ReplaceInline(TEXT(" "), TEXT(""));
		In.ReplaceInline(TEXT("\t"), TEXT(""));
		In.ReplaceInline(TEXT("("), TEXT(""));
		In.ReplaceInline(TEXT(")"), TEXT(""));
		// 全角括号（常见于中文输入法/模型输出）
		In.ReplaceInline(TEXT("（"), TEXT(""));
		In.ReplaceInline(TEXT("）"), TEXT(""));
		// 其它常见不可见空白
		In.ReplaceInline(TEXT("\u00A0"), TEXT("")); // nbsp
		In.ReplaceInline(TEXT("\u200B"), TEXT("")); // zero-width space
		return In;
	};
	const FString Norm = NormalizeDisplay(Trimmed);
	if (Norm.Equals(TEXT("EqualInt"), ESearchCase::IgnoreCase))
	{
		return TEXT("EqualEqual_IntInt");
	}
	if (Norm.Equals(TEXT("AddFloat"), ESearchCase::IgnoreCase))
	{
		return TEXT("Add_FloatFloat");
	}
	if (Norm.Equals(TEXT("AddInt"), ESearchCase::IgnoreCase) || Norm.Equals(TEXT("Int+Int"), ESearchCase::IgnoreCase))
	{
		return TEXT("Add_IntInt");
	}
	if (Norm.Equals(TEXT("SubtractFloat"), ESearchCase::IgnoreCase) || Norm.Equals(TEXT("Float-Float"), ESearchCase::IgnoreCase))
	{
		return TEXT("Subtract_FloatFloat");
	}
	return Trimmed;
}

/** 把“语义/运算表达式”转换为可尝试的真实 UFunction 名候选。 */
static void BuildFunctionNameCandidates(const FString& InFuncName, TArray<FString>& OutCandidates)
{
	OutCandidates.Reset();
	const FString Raw = InFuncName.TrimStartAndEnd();
	if (Raw.IsEmpty())
	{
		return;
	}

	AddUniqueString(OutCandidates, ResolveFunctionNameAlias(Raw));
	AddUniqueString(OutCandidates, Raw);

	// Blueprint 显示名运算符兜底（避免 alias key 因不可见字符 miss）
	{
		FString Disp = Raw;
		Disp.ReplaceInline(TEXT(" "), TEXT(""));
		Disp.ReplaceInline(TEXT("\t"), TEXT(""));
		Disp.ReplaceInline(TEXT("("), TEXT(""));
		Disp.ReplaceInline(TEXT(")"), TEXT(""));
		Disp.ReplaceInline(TEXT("（"), TEXT(""));
		Disp.ReplaceInline(TEXT("）"), TEXT(""));
		Disp.ReplaceInline(TEXT("\u00A0"), TEXT(""));
		Disp.ReplaceInline(TEXT("\u200B"), TEXT(""));
		if (Disp.Equals(TEXT("AddFloat"), ESearchCase::IgnoreCase))
		{
			AddUniqueString(OutCandidates, TEXT("Add_FloatFloat"));
		}
		else if (Disp.Equals(TEXT("EqualInt"), ESearchCase::IgnoreCase))
		{
			AddUniqueString(OutCandidates, TEXT("EqualEqual_IntInt"));
		}
		else if (Disp.Equals(TEXT("AddInt"), ESearchCase::IgnoreCase) || Disp.Equals(TEXT("Int+Int"), ESearchCase::IgnoreCase))
		{
			AddUniqueString(OutCandidates, TEXT("Add_IntInt"));
		}
		else if (Disp.Equals(TEXT("SubtractFloat"), ESearchCase::IgnoreCase) || Disp.Equals(TEXT("Float-Float"), ESearchCase::IgnoreCase))
		{
			AddUniqueString(OutCandidates, TEXT("Subtract_FloatFloat"));
		}
	}

	const FString K = NormalizeMathSemanticKey(Raw);
	if (K.Contains(TEXT("vector*float")) || K.Contains(TEXT("float*vector")))
	{
		AddUniqueString(OutCandidates, TEXT("Multiply_VectorFloat"));
		AddUniqueString(OutCandidates, TEXT("Multiply_FloatVector"));
	}
	if (K.Contains(TEXT("vector*vector")))
	{
		AddUniqueString(OutCandidates, TEXT("Multiply_VectorVector"));
	}
	if (K.Contains(TEXT("vector+vector")))
	{
		AddUniqueString(OutCandidates, TEXT("Add_VectorVector"));
	}
	if (K.Contains(TEXT("vector-vector")))
	{
		AddUniqueString(OutCandidates, TEXT("Subtract_VectorVector"));
	}
	if (K.Contains(TEXT("vector/float")))
	{
		AddUniqueString(OutCandidates, TEXT("Divide_VectorFloat"));
	}
	if (K.Contains(TEXT("float-float")) || K.Contains(TEXT("double-double")))
	{
		AddUniqueString(OutCandidates, TEXT("Subtract_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Subtract_DoubleDouble"));
	}
	if (K.Contains(TEXT("float+float")) || K.Contains(TEXT("double+double")))
	{
		AddUniqueString(OutCandidates, TEXT("Add_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Add_DoubleDouble"));
	}
	if (K.Contains(TEXT("float*float")) || K.Contains(TEXT("double*double")))
	{
		AddUniqueString(OutCandidates, TEXT("Multiply_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Multiply_DoubleDouble"));
	}
	if (K.Contains(TEXT("float/float")) || K.Contains(TEXT("double/double")))
	{
		AddUniqueString(OutCandidates, TEXT("Divide_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Divide_DoubleDouble"));
	}
	if (K.Contains(TEXT("int+int")) || K.Contains(TEXT("integer+integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Add_IntInt"));
	}
	if (K.Contains(TEXT("int-int")) || K.Contains(TEXT("integer-integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Subtract_IntInt"));
	}
	if (K.Contains(TEXT("int*int")) || K.Contains(TEXT("integer*integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Multiply_IntInt"));
	}
	if (K.Contains(TEXT("int/int")) || K.Contains(TEXT("integer/integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Divide_IntInt"));
	}
	if (K.Contains(TEXT("convfloattostring")) || K.Contains(TEXT("convdoubletostring")))
	{
		AddUniqueString(OutCandidates, TEXT("Conv_FloatToString"));
		AddUniqueString(OutCandidates, TEXT("Conv_DoubleToString"));
	}
	if (K.Contains(TEXT("rotator+rotator")))
	{
		AddUniqueString(OutCandidates, TEXT("Add_RotatorRotator"));
		AddUniqueString(OutCandidates, TEXT("ComposeRotators"));
	}
	if (K.Contains(TEXT("rotator-rotator")))
	{
		AddUniqueString(OutCandidates, TEXT("Subtract_RotatorRotator"));
		AddUniqueString(OutCandidates, TEXT("NormalizedDeltaRotator"));
	}
	if (K.Contains(TEXT("float>float")) || K.Contains(TEXT("double>double")) || K.Contains(TEXT("int>int")) || K.Contains(TEXT("integer>integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Greater_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Greater_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("Greater_IntInt"));
	}
	if (K.Contains(TEXT("float>=float")) || K.Contains(TEXT("double>=double")) || K.Contains(TEXT("int>=int")) || K.Contains(TEXT("integer>=integer")))
	{
		AddUniqueString(OutCandidates, TEXT("GreaterEqual_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("GreaterEqual_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("GreaterEqual_IntInt"));
	}
	if (K.Contains(TEXT("float<float")) || K.Contains(TEXT("double<double")) || K.Contains(TEXT("int<int")) || K.Contains(TEXT("integer<integer")))
	{
		AddUniqueString(OutCandidates, TEXT("Less_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("Less_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("Less_IntInt"));
	}
	if (K.Contains(TEXT("float<=float")) || K.Contains(TEXT("double<=double")) || K.Contains(TEXT("int<=int")) || K.Contains(TEXT("integer<=integer")))
	{
		AddUniqueString(OutCandidates, TEXT("LessEqual_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("LessEqual_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("LessEqual_IntInt"));
	}
	if (K.Contains(TEXT("float==float")) || K.Contains(TEXT("double==double")) || K.Contains(TEXT("int==int")) || K.Contains(TEXT("integer==integer")))
	{
		AddUniqueString(OutCandidates, TEXT("EqualEqual_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("EqualEqual_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("EqualEqual_IntInt"));
	}
	if (K.Contains(TEXT("float!=float")) || K.Contains(TEXT("double!=double")) || K.Contains(TEXT("int!=int")) || K.Contains(TEXT("integer!=integer")))
	{
		AddUniqueString(OutCandidates, TEXT("NotEqual_FloatFloat"));
		AddUniqueString(OutCandidates, TEXT("NotEqual_DoubleDouble"));
		AddUniqueString(OutCandidates, TEXT("NotEqual_IntInt"));
	}
}

// 柔性函数解析：优先在常用库/基类里查；找不到再对已加载的所有 UClass 进行一次扫描。
static UFunction* ResolveCallFunctionFlexibly(const FString& FuncName, UClass*& OutOwner)
{
	OutOwner = nullptr;

	FString QualifiedOwner;
	FString RawFunction = FuncName;

	// 优先处理蓝图显示名运算符（避免因为显示名/本地化/不可见字符导致的 miss）
	{
		FString Disp = RawFunction;
		Disp.TrimStartAndEndInline();
		Disp.ReplaceInline(TEXT(" "), TEXT(""));
		Disp.ReplaceInline(TEXT("\t"), TEXT(""));
		Disp.ReplaceInline(TEXT("("), TEXT(""));
		Disp.ReplaceInline(TEXT(")"), TEXT(""));
		Disp.ReplaceInline(TEXT("（"), TEXT(""));
		Disp.ReplaceInline(TEXT("）"), TEXT(""));
		Disp.ReplaceInline(TEXT("\u00A0"), TEXT(""));
		Disp.ReplaceInline(TEXT("\u200B"), TEXT(""));
		if (Disp.Equals(TEXT("AddFloat"), ESearchCase::IgnoreCase))
		{
			// 兼容不同引擎版本/命名：优先精确名，失败则在 UKismetMathLibrary 中扫描“2 float -> float”的 Add 实现
			if (UFunction* F = UKismetMathLibrary::StaticClass()->FindFunctionByName(FName(TEXT("Add_FloatFloat"))))
			{
				OutOwner = UKismetMathLibrary::StaticClass();
				return F;
			}
			for (TFieldIterator<UFunction> It(UKismetMathLibrary::StaticClass(), EFieldIteratorFlags::IncludeSuper); It; ++It)
			{
				UFunction* F = *It;
				if (!F)
				{
					continue;
				}
				const FString N = F->GetName();
				if (!N.Contains(TEXT("Add"), ESearchCase::IgnoreCase))
				{
					continue;
				}

				int32 NumericInputs = 0;
				bool bReturnNumeric = false;
				for (TFieldIterator<FProperty> PIt(F); PIt; ++PIt)
				{
					const FProperty* Prop = *PIt;
					if (!Prop)
					{
						continue;
					}
					const bool bIsReturn = Prop->HasAnyPropertyFlags(CPF_ReturnParm);
					const bool bIsParam = Prop->HasAnyPropertyFlags(CPF_Parm) && !Prop->HasAnyPropertyFlags(CPF_OutParm);
					const bool bIsNumeric = Prop->IsA<FFloatProperty>() || Prop->IsA<FDoubleProperty>();

					if (bIsReturn)
					{
						bReturnNumeric = bIsNumeric;
					}
					else if (bIsParam && bIsNumeric)
					{
						NumericInputs++;
					}
				}

				if (bReturnNumeric && NumericInputs >= 2)
				{
					OutOwner = UKismetMathLibrary::StaticClass();
					return F;
				}
			}
		}
		if (Disp.Equals(TEXT("EqualInt"), ESearchCase::IgnoreCase))
		{
			if (UFunction* F = UKismetMathLibrary::StaticClass()->FindFunctionByName(FName(TEXT("EqualEqual_IntInt"))))
			{
				OutOwner = UKismetMathLibrary::StaticClass();
				return F;
			}
			for (TFieldIterator<UFunction> It(UKismetMathLibrary::StaticClass(), EFieldIteratorFlags::IncludeSuper); It; ++It)
			{
				UFunction* F = *It;
				if (!F)
				{
					continue;
				}
				const FString N = F->GetName();
				if (!N.Contains(TEXT("Equal"), ESearchCase::IgnoreCase))
				{
					continue;
				}

				int32 IntInputs = 0;
				bool bReturnBool = false;
				for (TFieldIterator<FProperty> PIt(F); PIt; ++PIt)
				{
					const FProperty* Prop = *PIt;
					if (!Prop)
					{
						continue;
					}
					const bool bIsReturn = Prop->HasAnyPropertyFlags(CPF_ReturnParm);
					const bool bIsParam = Prop->HasAnyPropertyFlags(CPF_Parm) && !Prop->HasAnyPropertyFlags(CPF_OutParm);
					const bool bIsInt = Prop->IsA<FIntProperty>();

					if (bIsReturn)
					{
						bReturnBool = Prop->IsA<FBoolProperty>();
					}
					else if (bIsParam && bIsInt)
					{
						IntInputs++;
					}
				}
				if (bReturnBool && IntInputs >= 2)
				{
					OutOwner = UKismetMathLibrary::StaticClass();
					return F;
				}
			}
		}
		if (Disp.Equals(TEXT("AddInt"), ESearchCase::IgnoreCase) || Disp.Equals(TEXT("Int+Int"), ESearchCase::IgnoreCase))
		{
			if (UFunction* F = UKismetMathLibrary::StaticClass()->FindFunctionByName(FName(TEXT("Add_IntInt"))))
			{
				OutOwner = UKismetMathLibrary::StaticClass();
				return F;
			}
			for (TFieldIterator<UFunction> It(UKismetMathLibrary::StaticClass(), EFieldIteratorFlags::IncludeSuper); It; ++It)
			{
				UFunction* F = *It;
				if (!F)
				{
					continue;
				}
				const FString N = F->GetName();
				if (!N.Contains(TEXT("Add"), ESearchCase::IgnoreCase))
				{
					continue;
				}

				int32 IntInputs = 0;
				bool bReturnInt = false;
				for (TFieldIterator<FProperty> PIt(F); PIt; ++PIt)
				{
					const FProperty* Prop = *PIt;
					if (!Prop)
					{
						continue;
					}
					const bool bIsReturn = Prop->HasAnyPropertyFlags(CPF_ReturnParm);
					const bool bIsParam = Prop->HasAnyPropertyFlags(CPF_Parm) && !Prop->HasAnyPropertyFlags(CPF_OutParm);
					const bool bIsInt = Prop->IsA<FIntProperty>();

					if (bIsReturn)
					{
						bReturnInt = bIsInt;
					}
					else if (bIsParam && bIsInt)
					{
						IntInputs++;
					}
				}
				if (bReturnInt && IntInputs >= 2)
				{
					OutOwner = UKismetMathLibrary::StaticClass();
					return F;
				}
			}
		}
	}

	if (TrySplitQualifiedFunctionName(FuncName, QualifiedOwner, RawFunction))
	{
		if (UClass* ExplicitOwner = ResolveClassByLooseName(QualifiedOwner))
		{
			TArray<FString> Candidates;
			BuildFunctionNameCandidates(RawFunction, Candidates);
			for (const FString& C : Candidates)
			{
				if (UFunction* F = ExplicitOwner->FindFunctionByName(FName(*C)))
				{
					OutOwner = ExplicitOwner;
					return F;
				}
			}
		}
	}

	TArray<FString> Candidates;
	BuildFunctionNameCandidates(RawFunction, Candidates);
	if (Candidates.Num() == 0)
	{
		return nullptr;
	}

	// 常用库/基类优先（命中率最高，避免全量扫描）
	UClass* Priorities[] = {
		UKismetSystemLibrary::StaticClass(),
		UGameplayStatics::StaticClass(),
		UKismetMathLibrary::StaticClass(),
		UKismetStringLibrary::StaticClass(),
		AActor::StaticClass(),
		APawn::StaticClass(),
		ACharacter::StaticClass(),
	};
	for (UClass* C : Priorities)
	{
		if (!C)
		{
			continue;
		}
		for (const FString& Cand : Candidates)
		{
			if (UFunction* F = C->FindFunctionByName(FName(*Cand)))
			{
				OutOwner = C;
				return F;
			}
		}
	}

	// 兜底：遍历已加载的 UClass（通常规模有限，不算昂贵）
	for (TObjectIterator<UClass> It; It; ++It)
	{
		UClass* C = *It;
		if (!C)
		{
			continue;
		}
		for (const FString& Cand : Candidates)
		{
			if (UFunction* F = C->FindFunctionByName(FName(*Cand)))
			{
				OutOwner = C;
				return F;
			}
		}
	}

	return nullptr;
}

static bool ExecuteCreateNode(const FBlueprintDslActionStep& Step, UBlueprint* Blueprint, UEdGraph* Graph, FString& OutError)
{
	if (!Graph)
	{
		OutError = TEXT("目标图为空。");
		return false;
	}

	if (FindNodeByTaggedNodeId(Graph, Step.NodeId))
	{
		// 已存在则视为成功（便于重复执行/断点执行）
		return true;
	}

	const FString NodeType = Step.NodeType.TrimStartAndEnd();
	if (NodeType.IsEmpty())
	{
		OutError = TEXT("CreateNode.nodeType 为空。");
		return false;
	}

	UEdGraphNode* NewNode = nullptr;

	// 简单布局：按节点数量右移
	int32 PosX = 200;
	int32 PosY = 200;
	{
		int32 Existing = 0;
		for (UEdGraphNode* N : Graph->Nodes)
		{
			if (N) { ++Existing; }
		}
		PosX += Existing * 40;
		PosY += Existing * 20;
	}

	if (NodeType.Equals(TEXT("Branch"), ESearchCase::IgnoreCase))
	{
		UK2Node_IfThenElse* Node = NewObject<UK2Node_IfThenElse>(Graph);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("Sequence"), ESearchCase::IgnoreCase))
	{
		UK2Node* Node = CreateK2NodeByClassPathOrName(Graph, TEXT("K2Node_ExecutionSequence"));
		if (!Node)
		{
			OutError = TEXT("无法创建 Sequence 节点（未找到 K2Node_ExecutionSequence 类）。");
			return false;
		}
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("DoOnce"), ESearchCase::IgnoreCase))
	{
		UK2Node* Node = CreateK2NodeByClassPathOrName(Graph, TEXT("K2Node_DoOnce"));
		if (!Node)
		{
			OutError = TEXT("无法创建 DoOnce 节点（未找到 K2Node_DoOnce 类）。");
			return false;
		}
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("Gate"), ESearchCase::IgnoreCase))
	{
		UK2Node* Node = CreateK2NodeByClassPathOrName(Graph, TEXT("K2Node_Gate"));
		if (!Node)
		{
			OutError = TEXT("无法创建 Gate 节点（未找到 K2Node_Gate 类）。");
			return false;
		}
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("Delay"), ESearchCase::IgnoreCase))
	{
		// 避免依赖 UK2Node_Delay 头文件路径差异：用 CallFunction 生成 Delay（latent）节点。
		UFunction* TargetFunc = UKismetSystemLibrary::StaticClass()->FindFunctionByName(FName(TEXT("Delay")));
		if (!TargetFunc)
		{
			OutError = TEXT("未找到 UKismetSystemLibrary::Delay。");
			return false;
		}

		UK2Node_CallFunction* Node = NewObject<UK2Node_CallFunction>(Graph);
		Node->SetFromFunction(TargetFunc);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("InputKey"), ESearchCase::IgnoreCase))
	{
		FKey InputKey;
		if (!TryResolveInputKeyForCreateStep(Step, InputKey))
		{
			OutError = TEXT("InputKey 需要可解析的按键名。请在 functionName 中填写键名（例如 E/F/SpaceBar），或让 nodeId 使用 InputKey_E 这类语义命名。");
			return false;
		}

		UK2Node_InputKey* Node = nullptr;
		if (!EnsureInputKeyEvent(Graph, InputKey, PosX, PosY, Node, OutError))
		{
			return false;
		}
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("CallFunction"), ESearchCase::IgnoreCase))
	{
		const FString Func = Step.FunctionName.TrimStartAndEnd();
		if (Func.IsEmpty())
		{
			OutError = TEXT("CallFunction.functionName 为空。");
			return false;
		}

		// BeginPlay / AnyDamage 始终按事件处理，而不是 CallFunction
		if (Func.Equals(TEXT("ReceiveBeginPlay"), ESearchCase::IgnoreCase) ||
			Func.Equals(TEXT("BeginPlay"), ESearchCase::IgnoreCase))
		{
			UK2Node_Event* BeginPlayNode = nullptr;
			if (!EnsureBeginPlayEvent(Graph, PosX, PosY, BeginPlayNode, OutError))
			{
				return false;
			}
			NewNode = BeginPlayNode;
		}
		else if (IsAnyDamageNodeId(Func))
		{
			UK2Node_Event* AnyDamageNode = nullptr;
			if (!EnsureAnyDamageEvent(Graph, PosX, PosY, AnyDamageNode, OutError))
			{
				return false;
			}
			NewNode = AnyDamageNode;
		}
		// LLM 常把按键事件写成 CallFunction（InputAction E / Input Key F）
		else if (LooksLikeMiswiredInputKeyCall(Func) ||
				 LooksLikeMiswiredInputKeyCall(Step.NodeId) ||
				 LooksLikeMiswiredInputKeyCall(Step.TargetClass))
		{
			FKey InputKey;
			if (!TryResolveInputKeyForCreateStep(Step, InputKey))
			{
				OutError = TEXT("InputAction/InputKey 需要可解析的按键名。请在 functionName 中填写键名（例如 E/F/SpaceBar），或使用 InputAction_E / InputKey_E 这类 nodeId。");
				return false;
			}
			UK2Node_InputKey* KeyNode = nullptr;
			if (!EnsureInputKeyEvent(Graph, InputKey, PosX, PosY, KeyNode, OutError))
			{
				return false;
			}
			NewNode = KeyNode;
		}
		// Get Component by Class（纯函数，targetClass 写组件类名）
		else if (Func.Equals(TEXT("GetComponentByClass"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Get Component by Class"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Get Component By Class"), ESearchCase::IgnoreCase))
		{
			const FString CompClassName = Step.TargetClass.TrimStartAndEnd();
			if (CompClassName.IsEmpty())
			{
				OutError = TEXT("GetComponentByClass 需要 targetClass（组件类名，如 SceneComponent / StaticMeshComponent）。");
				return false;
			}
			UK2Node_CallFunction* CompNode = nullptr;
			if (!TryCreateGetComponentByClassNode(Graph, CompClassName, PosX, PosY, CompNode, OutError))
			{
				return false;
			}
			NewNode = CompNode;
		}
		else if (LooksLikeSetTimerByFunctionNameCall(Func))
		{
			const bool bNextTick =
				Func.Contains(TEXT("Next Tick"), ESearchCase::IgnoreCase) ||
				Func.Contains(TEXT("NextTick"), ESearchCase::IgnoreCase);
			const FString Callback = Step.VarName.TrimStartAndEnd();
			if (Callback.IsEmpty())
			{
				OutError = TEXT("Set Timer by Function Name（CallFunction）需要 varName 指定回调函数名（如 OnTimerTick）。");
				return false;
			}
			UK2Node_CallFunction* TimerNode = nullptr;
			if (!TryCreateSetTimerByFunctionNameNode(
					Graph,
					Callback,
					Step.DefaultValue,
					ParseOptionalLoopingFromStep(Step),
					bNextTick,
					PosX,
					PosY,
					TimerNode,
					OutError))
			{
				return false;
			}
			NewNode = TimerNode;
		}
		// 模型常误写：把 Overlap 事件当作 CallFunction。这里直接按事件节点创建/复用，避免“未找到函数”连锁失败。
		else if (Func.Equals(TEXT("OnComponentBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("ComponentBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("EventComponentBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Event_ComponentBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Event_OnComponentBeginOverlap"), ESearchCase::IgnoreCase))
		{
			UK2Node_Event* OverlapNode = nullptr;
			if (Blueprint && EnsureComponentBeginOverlapEvent(Blueprint, Graph, PosX, PosY, OverlapNode, OutError) && OverlapNode)
			{
				NewNode = OverlapNode;
			}
			else
			{
				// 退化：没有可绑定组件时，改为 ActorBeginOverlap（保证可执行）
				FString FallbackErr;
				UK2Node_Event* ActorOverlapNode = nullptr;
				if (EnsureActorBeginOverlapEvent(Graph, PosX, PosY, ActorOverlapNode, FallbackErr) && ActorOverlapNode)
				{
					NewNode = ActorOverlapNode;
				}
				else
				{
					OutError = FString::Printf(TEXT("无法创建 OnComponentBeginOverlap：%s"), *OutError);
					return false;
				}
			}
		}
		else if (Func.Equals(TEXT("OnActorBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("ActorBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("EventActorBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Event_ActorBeginOverlap"), ESearchCase::IgnoreCase) ||
				 Func.Equals(TEXT("Event_OnActorBeginOverlap"), ESearchCase::IgnoreCase))
		{
			UK2Node_Event* OverlapNode = nullptr;
			if (!EnsureActorBeginOverlapEvent(Graph, PosX, PosY, OverlapNode, OutError))
			{
				return false;
			}
			NewNode = OverlapNode;
		}
		else
		{
			// 方案 3：高风险黑名单 —— 必须 requiresConfirmation=true 才放行
			const FString Resolved = ResolveFunctionNameAlias(Func);
			if (IsHighRiskFunctionName(Resolved) && !Step.bRequiresConfirmation)
			{
				OutError = FString::Printf(
					TEXT("函数 %s 属于高风险操作（黑名单），DSL 必须显式设置 requiresConfirmation=true 才能执行。"),
					*Resolved);
				return false;
			}

			UClass* OwnerClass = nullptr;
			UFunction* TargetFunc = ResolveCallFunctionFlexibly(Func, OwnerClass);
			if (!TargetFunc)
			{
				OutError = FString::Printf(
					TEXT("未找到函数：%s（请检查拼写；对于 Actor 系列请使用蓝图名，如 K2_GetActorLocation 或别名 GetActorLocation）。"),
					*Func);
				return false;
			}

			UK2Node_CallFunction* Node = NewObject<UK2Node_CallFunction>(Graph);
			Node->SetFromFunction(TargetFunc);
			Graph->AddNode(Node, true, false);
			Node->NodePosX = PosX;
			Node->NodePosY = PosY;
			Node->CreateNewGuid();
			Node->AllocateDefaultPins();
			Node->PostPlacedNewNode();
			NewNode = Node;

			// Trace 族自动写合理默认值（Visibility/No debug/Ignore self）
			if (Resolved.Contains(TEXT("Trace"), ESearchCase::IgnoreCase))
			{
				ApplyTraceDefaults(Node);
			}
		}
	}
	else if (NodeType.Equals(TEXT("SpawnActorFromClass"), ESearchCase::IgnoreCase))
	{
		// 高风险：建议 DSL 设置 requiresConfirmation=true（ValidateSteps 会提示 warning）
		// class 名可以写在 targetClass（首选）或 functionName（兼容老 Prompt）。
		FString ClassName = Step.TargetClass.TrimStartAndEnd();
		if (ClassName.IsEmpty())
		{
			ClassName = Step.FunctionName.TrimStartAndEnd();
		}
		FString RejectReason;
		UClass* SpawnClass = ResolveWhitelistedSpawnClassByName(ClassName, &RejectReason);
		if (!SpawnClass)
		{
			OutError = FString::Printf(TEXT("SpawnActorFromClass class=%s 被拒绝：%s"), *ClassName, *RejectReason);
			return false;
		}

		UK2Node_SpawnActorFromClass* Node = NewObject<UK2Node_SpawnActorFromClass>(Graph);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();

		// 设置 Class pin
		if (UEdGraphPin* ClassPin = FindPinLoose(Node, TEXT("Class")))
		{
			ClassPin->Modify();
			ClassPin->DefaultObject = SpawnClass;
		}

		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("Cast"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("CastTo"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("DynamicCast"), ESearchCase::IgnoreCase))
	{
		FString TargetClassName = Step.TargetClass.TrimStartAndEnd();
		if (TargetClassName.IsEmpty())
		{
			TargetClassName = Step.FunctionName.TrimStartAndEnd();
		}

		UClass* TargetCls = nullptr;
		if (!TargetClassName.IsEmpty())
		{
			TargetCls = ResolveClassByName(TargetClassName);
			if (!TargetCls)
			{
				TargetCls = ResolveClassByLooseName(TargetClassName);
			}
		}
		if (!TargetCls)
		{
			TargetCls = InferCastTargetClassFromHints(Step.NodeId, Step.TargetClass, Step.FunctionName);
		}
		if (!TargetCls)
		{
			OutError = TEXT("Cast 需要 targetClass（可用蓝图短名如 BP_Player，或 nodeId 含 Character/Pawn 以便推断）。");
			return false;
		}

		UK2Node_DynamicCast* Node = NewObject<UK2Node_DynamicCast>(Graph);
		Node->TargetType = TargetCls;
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("IsValid"), ESearchCase::IgnoreCase))
	{
		// 函数版 IsValid：UKismetSystemLibrary::IsValid(Object) → bool。调用方需要再接 Branch。
		UFunction* IsValidFunc = UKismetSystemLibrary::StaticClass()->FindFunctionByName(TEXT("IsValid"));
		if (!IsValidFunc)
		{
			OutError = TEXT("未找到 UKismetSystemLibrary::IsValid 函数（当前引擎版本可能不兼容）。");
			return false;
		}

		UK2Node_CallFunction* Node = NewObject<UK2Node_CallFunction>(Graph);
		Node->SetFromFunction(IsValidFunc);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("MakeVector"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("BreakVector"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("MakeRotator"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("BreakRotator"), ESearchCase::IgnoreCase))
	{
		// 使用 UKismetMathLibrary 专用函数，避免泛型 UK2Node_MakeStruct/BreakStruct 对 FVector/FRotator 产生编译警告
		const TCHAR* FuncName =
			NodeType.Equals(TEXT("MakeVector"), ESearchCase::IgnoreCase) ? TEXT("MakeVector") :
			NodeType.Equals(TEXT("BreakVector"), ESearchCase::IgnoreCase) ? TEXT("BreakVector") :
			NodeType.Equals(TEXT("MakeRotator"), ESearchCase::IgnoreCase) ? TEXT("MakeRotator") :
			TEXT("BreakRotator");

		UFunction* TargetFunc = UKismetMathLibrary::StaticClass()->FindFunctionByName(FuncName);
		if (!TargetFunc)
		{
			OutError = FString::Printf(TEXT("未找到 UKismetMathLibrary::%s"), FuncName);
			return false;
		}

		UK2Node_CallFunction* Node = NewObject<UK2Node_CallFunction>(Graph);
		Node->SetFromFunction(TargetFunc);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("ForEachLoop"), ESearchCase::IgnoreCase) ||
	         NodeType.Equals(TEXT("ForEachLoopWithBreak"), ESearchCase::IgnoreCase))
	{
		const FString MacroName = NodeType.Equals(TEXT("ForEachLoopWithBreak"), ESearchCase::IgnoreCase)
			? TEXT("ForEachLoopWithBreak")
			: TEXT("ForEachLoop");

		UEdGraph* MacroGraph = LoadStandardMacroGraphByName(MacroName);
		if (!MacroGraph)
		{
			OutError = FString::Printf(TEXT("未找到引擎 StandardMacros 中的宏图：%s（请确认引擎内容可用）。"), *MacroName);
			return false;
		}

		UK2Node_MacroInstance* Node = NewObject<UK2Node_MacroInstance>(Graph);
		Node->SetMacroGraph(MacroGraph);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("SwitchOnInt"), ESearchCase::IgnoreCase) ||
	         NodeType.Equals(TEXT("SwitchInt"), ESearchCase::IgnoreCase))
	{
		UK2Node_SwitchInteger* Node = NewObject<UK2Node_SwitchInteger>(Graph);

		// cases 约定：连续整数序列。StartIndex 取 cases[0]，case 数量取 cases.Num()。
		int32 Start = 0;
		if (Step.CaseValues.Num() > 0)
		{
			Start = FCString::Atoi(*Step.CaseValues[0]);
		}
		Node->StartIndex = Start;

		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();

		// 调整 case pin 数量
		const int32 Desired = FMath::Max(0, Step.CaseValues.Num());
		auto CountCaseExecPins = [&]() -> int32
		{
			int32 Count = 0;
			for (UEdGraphPin* P : Node->Pins)
			{
				if (!P) continue;
				if (P->Direction != EGPD_Output) continue;
				if (P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec) continue;
				if (P->PinName == UK2Node_Switch::GetSelectionPinName()) continue;
				if (P->PinName == TEXT("Default")) continue;
				++Count;
			}
			return Count;
		};
		for (int32 Safety = 0; Safety < 32 && CountCaseExecPins() < Desired; ++Safety)
		{
			Node->AddPinToSwitchNode();
		}
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("SwitchOnString"), ESearchCase::IgnoreCase) ||
	         NodeType.Equals(TEXT("SwitchString"), ESearchCase::IgnoreCase))
	{
		UK2Node_SwitchString* Node = NewObject<UK2Node_SwitchString>(Graph);
		Node->PinNames.Reset();
		for (const FString& V : Step.CaseValues)
		{
			const FString S = V.TrimStartAndEnd();
			if (!S.IsEmpty())
			{
				Node->PinNames.Add(FName(*S));
			}
		}

		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("Select"), ESearchCase::IgnoreCase))
	{
		UK2Node_Select* Node = NewObject<UK2Node_Select>(Graph);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();

		// cases 可选：用于扩展 option pins 数量（默认 2）。不处理 enum 绑定，仅增加输入项数量。
		int32 DesiredOptions = Step.CaseValues.Num();
		if (DesiredOptions < 2) DesiredOptions = 2;
		TArray<UEdGraphPin*> OptionPins;
		Node->GetOptionPins(OptionPins);
		for (int32 Safety = 0; Safety < 32 && OptionPins.Num() < DesiredOptions; ++Safety)
		{
			Node->AddInputPin();
			OptionPins.Reset();
			Node->GetOptionPins(OptionPins);
		}

		// 供 SetPinDefault/连线容错：把 cases 标签写入注释，便于 pinName=A 映射到 Option 0
		if (Step.CaseValues.Num() > 0)
		{
			AppendSelectCasesMetadata(Node, Step.CaseValues);
		}

		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("MakeArrayInt"), ESearchCase::IgnoreCase) ||
	         NodeType.Equals(TEXT("MakeArray"), ESearchCase::IgnoreCase))
	{
		UK2Node_MakeArray* Node = NewObject<UK2Node_MakeArray>(Graph);
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();

		// cases 用作元素数量（>=1）；值本身用后续 SetPinDefault 写入各元素 pin。
		Node->NumInputs = FMath::Max(1, Step.CaseValues.Num() > 0 ? Step.CaseValues.Num() : 3);

		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();

		// 若指定 MakeArrayInt，则把输出 pin 类型锁到 int，避免野生 wildcard 影响后续连线
		if (NodeType.Equals(TEXT("MakeArrayInt"), ESearchCase::IgnoreCase))
		{
			LockMakeArrayIntTypes(Node);
		}

		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("GetComponentByClass"), ESearchCase::IgnoreCase))
	{
		FString CompClassName = Step.TargetClass.TrimStartAndEnd();
		if (CompClassName.IsEmpty())
		{
			CompClassName = Step.FunctionName.TrimStartAndEnd();
		}
		if (CompClassName.IsEmpty())
		{
			OutError = TEXT("GetComponentByClass 需要 targetClass（组件类名，如 SceneComponent / StaticMeshComponent）。");
			return false;
		}

		UK2Node_CallFunction* CompNode = nullptr;
		if (!TryCreateGetComponentByClassNode(Graph, CompClassName, PosX, PosY, CompNode, OutError))
		{
			return false;
		}
		NewNode = CompNode;
	}
	else if (NodeType.Equals(TEXT("SetTimerByFunctionName"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("SetTimer"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("TimerByFunctionName"), ESearchCase::IgnoreCase) ||
			 NodeType.Equals(TEXT("SetTimerForNextTick"), ESearchCase::IgnoreCase))
	{
		const bool bNextTick =
			NodeType.Contains(TEXT("NextTick"), ESearchCase::IgnoreCase) ||
			NodeType.Contains(TEXT("Next Tick"), ESearchCase::IgnoreCase);
		const FString Callback = Step.FunctionName.TrimStartAndEnd();
		UK2Node_CallFunction* TimerNode = nullptr;
		if (!TryCreateSetTimerByFunctionNameNode(
				Graph,
				Callback,
				Step.DefaultValue,
				ParseOptionalLoopingFromStep(Step),
				bNextTick,
				PosX,
				PosY,
				TimerNode,
				OutError))
		{
			return false;
		}
		NewNode = TimerNode;
	}
	else if (NodeType.Equals(TEXT("GetVariable"), ESearchCase::IgnoreCase))
	{
		const FString Var = Step.VarName.TrimStartAndEnd();
		if (Var.IsEmpty())
		{
			OutError = TEXT("GetVariable.varName 为空。");
			return false;
		}

		UK2Node_VariableGet* Node = NewObject<UK2Node_VariableGet>(Graph);
		Node->VariableReference.SetSelfMember(FName(*Var));
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;
	}
	else if (NodeType.Equals(TEXT("SetVariable"), ESearchCase::IgnoreCase))
	{
		const FString Var = Step.VarName.TrimStartAndEnd();
		if (Var.IsEmpty())
		{
			OutError = TEXT("SetVariable.varName 为空。");
			return false;
		}

		UK2Node_VariableSet* Node = NewObject<UK2Node_VariableSet>(Graph);
		Node->VariableReference.SetSelfMember(FName(*Var));
		Graph->AddNode(Node, true, false);
		Node->NodePosX = PosX;
		Node->NodePosY = PosY;
		Node->CreateNewGuid();
		Node->AllocateDefaultPins();
		Node->PostPlacedNewNode();
		NewNode = Node;

		// v2: valueFrom 自动连到 Set 节点的 Value pin（若提供）
		if (!Step.ValueFromNodeId.IsEmpty() && !Step.ValueFromPin.IsEmpty())
		{
			UEdGraphNode* FromNode = FindNodeByTaggedNodeId(Graph, Step.ValueFromNodeId);
			if (FromNode)
			{
				UEdGraphPin* FromPin = FromNode->FindPin(Step.ValueFromPin);
				if (FromPin)
				{
					UEdGraphPin* ToPin = Node->FindPin(FName(*Var));
					if (!ToPin)
					{
						ToPin = Node->FindPin(TEXT("Value"));
					}
					if (!ToPin)
					{
						for (UEdGraphPin* P : Node->Pins)
						{
							if (P && P->Direction == EGPD_Input && P->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
							{
								ToPin = P;
								break;
							}
						}
					}

					if (ToPin)
					{
						if (const UEdGraphSchema* Schema = FromPin->GetSchema())
						{
							Schema->TryCreateConnection(FromPin, ToPin);
						}
					}
				}
			}
		}

		if (!Step.DefaultValue.IsEmpty())
		{
			TryApplyVariableSetLiteralValue(Node, Var, Step.DefaultValue);
		}
	}
	else
	{
		OutError = FString::Printf(TEXT("CreateNode.nodeType=%s 不受支持（白名单：Branch/Sequence/DoOnce/Gate/Delay/InputKey/GetComponentByClass/SetTimerByFunctionName/ForEachLoop/ForEachLoopWithBreak/SwitchOnInt/SwitchOnString/Select/MakeArrayInt/MakeArray/CallFunction/SpawnActorFromClass/Cast/IsValid/MakeVector/BreakVector/MakeRotator/BreakRotator/GetVariable/SetVariable）。"), *NodeType);
		return false;
	}

	if (!NewNode)
	{
		OutError = TEXT("创建节点失败。");
		return false;
	}

	TagNodeWithNodeId(NewNode, Step.NodeId);
	NewNode->SnapToGrid(16);
	return true;
}

